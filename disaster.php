<p>large scale in India. Most of the embankments constructed in north India where the Brahmaputra valley of Assam, northern parts of Bihar, Uttar Pradesh (Gangs, Yamuna and Ghaghara) and Punjab (Satluj, Beas and Ravi) are the main beneficiaries. In south India, embankments have been constructed mainly in the deltaic parts 61 the Mahanadi, the Godavari, the Krishna and along the banks of Penneru.</p>
<p>Flood Plain Zoning (FPZ): Flood plain zoning is another very effective method of flood management. It is based on information regarding flood plains, particularly the identification of flood ways in relation to land use. Detailed maps of flood prone areas are prepared after a thorough study of flood cycles. Some areas are more prone to floods than the others. Different zones are identified and demarcated. After that necessary control is exercised with respect to land use. A&bull; is the main channel of the river. It is the flood way which is totally prohibited zone. No construction is allowed in this zone. Area &#39;BB&#39; is the regulatory flowing fringe and marks the extent inundation design flood. This is called restrictive zone. Beyond that is the secondary hazard zone marked by &#39;CC&#39;. This is the extent of largest flood expected.</p>
