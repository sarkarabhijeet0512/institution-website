  <div class="menu-content Disaster"><h1>Disasters</h1><hr>
<p>A disaster is a sudden, calamitOus event that seriously disrupts the functioning of a community or society and causes human, material, and economic or environmental losses that . exceed the community&#39;s or society&#39;s ability to cope using its - own resources. Though often caused by nature, disasters can &bull; have human origins.</p>
<p>A disaster occurs when a hazard impact&#39;s on vulnerable people. Hazards are threatening event, or probability of occurrence of a potentially damaging phenomenon within a given time period and area. The combination of hazards, vulnerability and inability to reduce the potential negative consequences of risk results in disaster. Developing countries suffer the greatest costs when a disaster. hits.</p>
<p><br></p></div>
  <div class="menu-content Between"><h1>Difference between Hazard and Disaster</h1><hr>
<p>&bull; Hazards and disasters are closely related and are sometimes used as synonymous to each other. Hazard is a .threat, while disaster is an event. The latter is a calamity or tragedy or a consequence of a hazard. Natural hazards that cause great loss to human life and economy are known as disasters and catastrophes. The term, &#39;disaster&#39; is sometimes also used to describe a &quot;catastrophic situation in which the normal patterns of life (or ecosystems) have been disrupted and extraordinary emergency interventions are required to save and preserve human lives and/or the environment.&quot;</p>
</div>
  <div class="menu-content Types"><<h1>Types of Hazards</h1>
<hr>
<p>Types of Hazards depending upon their. causes ,Hazards could be caused by nature or man and are consequently called natural and man-made hazards.</p>
<p><strong>Natural Hazards</strong></p>
<p>These are caused by the forces of nature and man has no role to play in such hazards. They are naturally occurring physical phenomena caused either by rapid or slow onset events which can be geophysical (earthquakes, landslides, tsunamis and volcanic activity), hydrological (avalanches and floods), climatological (extreme temperatures, drought and . wildfires), meteorological (cyclones and storms/wave surges) or biological (disease epidemics and insect/animal plagues).</p>
<p><strong>Man-made Hazards</strong></p>
<p>Technological or man-made hazards (complex emergencies/conflicts, famine, displaced populations, industrial accidents and transport accidents) are events that are caused by humans and occur in or close to human settlements. This can include environmental degradation, pollution and accidents. Technological or man-made hazards (complex emergencies/conflicts, famine, displaced populations, industrial accidents and transport accidents).</p>
<p><strong>Socio-natural Hazards</strong></p>
<p>These are caused by the combined effect of natural forces and misdeeds of man. For example, the frequency and intensity of floods and droughts may increase due to indiscriininate felling of trees, particularly in the catchment areas of the rivers. Storm surge&#39;hazards may be Worsened by the destruction of mangroves. Although landslides Are normally caused by natural forces, yet they may be caused and their frequency and impact may be aggravated as a result of building roads in mountainous areas, excavating tunnels and by mining and quarrying.</p>
<p><strong>Vulnerability</strong></p>
<p>Hazards and disasters are unevenly distributed with respect to time and space. The terms likeearthquake prone&#39;, &#39;drought prone&#39; or &#39;flood prone&#39; areas are used to describe the distributional effect of the concerned hazards*.</p>
<p>People living in such areas are vulnerable to hazards and disaster of varied types. Thus, vulnerability is the extent to Which an individual or a community or an area is exposed to the impact of a hazard. The concept of vulnerability therefore implies a measure of risk combined with the level or social and economic ability to cope with the resulting event in Order to resist major disruption or loss.</p>
