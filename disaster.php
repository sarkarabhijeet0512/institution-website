  <div class="menu-content Flood">
<p>Floods</p>
<p>Flood is a state of high water level along a river channel or on coast that leads to inundation of land which is normally submerged. Flood is an important component of hydrological cycle of a drainage basin. As a matter of fact droughts and floods are two extreme ends of the hydrological cycle. While droughts occur due to failure of rainfall caused by the southwest monsoons, floods occur in the event of excessive r&#39;ainfall.</p>
<p>In modern times, annual floods due to swollen rivers have become severe causing untold loss to human beings, animals and property. This happens with sickening frequency especially in Assam, Bihar and Uttar Pradesh. Other parts of the country too are subjected to floods due to over flowing rivers, streams and tanks that overflow due to heavy intake.</p>
<p>Causes of Floods</p>
<p>Following are the major causes of floods in India: Meteorological Factors</p>
<p>&bull; Heavy Rainfall</p>
<p>&bull; Tropical Cyclones</p>
<p>&bull; Cloud Burst</p>
<p>Physical Factors</p>
<p>&bull; Large Catchment Area Human Factors</p>
<p>&bull; Deforestation</p>
<p>&bull; Faulty Agricultural Practices</p>
<p>&bull; Bursting of Danis</p>
<p>&bull; inadequate Drainage Arrangement</p>
<p>&bull; Siltation</p>
<p>&bull; Faulty Irrigation Practices</p>
<p>&bull; Accelerated Urbanisation</p>

<p>Flood Disaster Management</p>
<p>The main stemps for flood disaster management are briefly discussed below:</p>
<p>Flood Forecasting: Flood forecasting involent giving prior information regarding the occurrence of &#39;floods. This is essential and is extremely useful for taking timely action to prevent loss of human lives, livestock and movable property. The Central Water Commission (CWC) started flood forecasting in November 1958 when the first forecasting station was established at Old Railway Bridge, Delhi. Since then it has been extended to cover almost all the major inter-state flood prone rivers of the country. At !resent there are 100 flood forecasting stations on various rivers in the country which includes is. level foreatsting and 52 inflow forecasting stations.</p>
<p>Reduction of Runoff: Reduction of runoff is one of the very effective methods of flood disaster management. Runoff can be reduced by inducing and increasing infiltration of the surface seater into the ground in the catchment area. This can be done by large scale afforestation particularly in the catchment area. In the ludo-Gangetic plain, the runoff can be reduced by artificially inducing infiltration by digging wells along the beds of ephermal channels. A series of dug wells helps in storing and channelising the surface water.</p>
<p>Reducing Flood Peaks by Volume Reduction (Constructing Dams and Detention Basins): The flood peaks can be reduced by construction of dams and detention basins. Dams &bull;have the capacity of holding huge quantity of water during the flood period and help in reducing flood peak volume of water. Water stored in reservoirs created by constructing dams can be allowed to flow down the stream under controlled conditions depending upon the accommodating capacity of the river downstream the dam.</p>
<p>Reducing Flood Levels</p>
<p>&bull; Stream Channelisation: A close network of canals reduces flood hazard to a great extent because flood water flowing in the river can be diverted to canals.</p>
<p>&bull; Channel Improvement: Channel improvement is done by deepening, widening, straightening, lining and cleaning out of vegetation and debris from the river channel. These changes in the river channel increase the flood conveyance capacity of the river.</p>
<p>&bull; Flood Diversion: Flood diversion is the process. of diverting the flood water in marshes, lakes, the depressions and spreading it thinly over paddy fields and desert drylands. One such scheme in the Ghaggar Reversion &nbsp;Scheme which diverts 340 cu mecs (cubic metres per</p>
