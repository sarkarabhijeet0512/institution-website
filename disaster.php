<h1>Sendai Framework</h1>
<hr>
<p>The Sendai Framework is a 15-year, voluntary, non-binding agreement which recognises that the State has the primary role to reduce disaster risk but that responsibility should be shared with otlier stakeholders including local government, the private sector and other stakeholders.</p>
<p><strong>The Seven Global Targets</strong></p>
<ul>
    <li>Substantially reduce global disaster mortality by 2030, aiming to lower average per !COMO global mortality rate in the decade 2020-2030 compared to the period 2005-2015.</li>
    <li>Substantially reduce the number of affected people globally by 2030, aiming to lower average global figure per 100,000 in the decade 209.0-2030 compared to the period 2005-2015.</li>
    <li>Reduce direct disaster &#39;economic loss in relation to global Gross Domestic Product (GDP) by 2030.</li>
    <li>Substantially reduce disaster damage to critical infrastructure and disruption of basic services, among them health and educational facilities, including through developing their resilience by 2030.</li>
    <li>Substantially increase the number of countries with national and local disaster risk reduction strategies by 2020.</li>
    <li>Substantially enhance international cooperation to developing countries through adequate and sustainable support to complement their national actions for implementation of this Framework by 2030. Substantially increase the availability of and access to multi-hazard early warning systems and disaster risk information and assessments to the people by 2030.</li>
</ul>
<p><strong>The Four Priorities for Action</strong></p>
<p><strong>Priority 1. Understanding disaster risk</strong></p>
<p>Disaster risk management should be based on an understanding of disaster risk in all its dimensions of vulnerability, capacity, exposure of persons and assets, hazard characteristics and the environment.</p>
<p><strong>Priority 2. Strengthening disaster risk governance to manage disaster risk</strong></p>
<p>Disaster risk governance at the national, regional and global levels is very important for prevention, mitigation, preparedness, response, recovery and rehabilitation. It fosters Collaboration and partnership.</p>
<p>&nbsp;<strong>Priority 3. Investing in disaster risk reduction for resilience</strong></p>
<p>Public and private investment in disaster risk prevention and reduction through structural and non-structural measures are essential to enhance the economic, social, health and cultural resilience of persons, communities, countries and their assets, as well as the environment.</p>
<p><strong>Priority 4. Enhancing disaster preparedness for effective response and to &quot;Build Back Better&quot; in recovery, rehabilitation and reconstruction</strong></p>
<p>The growth of disaster risk means there is a need to strengthen disaster preparedness for response, take action in anticipation of events, and ensure capacities are in place for effective response and recovery at all levels. The recovery, rehabilitation and reconstruction phase is a critical opportunity to build back better, including through integrating disaster risk reduction into developinent creatures.</p>
<p><strong>Post Sendai Initiatives and Way Forward .</strong></p>
<p>March 18, 2015 was a historical day as it witnessed adoption of a 15-year plan by 188 UN Member Nations, including India. The plan termed as Sendai Framework&#39; was adopted during the 3rd UN World Conference on disaster risk reduction, held in the Japanese city of Sendai in 2015. The expected outcome of the framework is to prevent creation of new disaster risks and also to substantially reduce disaster risks. The four priority areas of Sendai Framework include:</p>
<ul>
    <li>Understanding Risk;</li>
    <li>Strengthening Risk Governance;</li>
    <li>Investing in disaster resilience; and</li>
    <li>Improving capacities for disaster response as well as for building back better after disasters.</li>
</ul>
<p>The Sendai Framework has set targets for substantial reduction in losses including reduction in number of deaths from disasters, number of people affected by disasters, economic losses and infrastructure losses. The new Framework calls for concrete indicators of progress towards these targets to be measured against the disaster losses in the decade after the adoption of the 2005 Hyogo Framework for Action (HFA), its predecessor.</p>
<p>In furtherance to its commitment to the Sendai framework, Government has taken up several important initiatives post Sendai Declaration. Firstly, as committed during the Sendai conference, India has successfully hosted the Asian in Ministerial Conference on Disaster Reduction (AMCDRR) November, 2016 and adopted &#39;New Delhi Declaration and &#39;Regional Action Plan for implementation of the Sendai Framework&#39;.</p>
<p>Secondly, Government of India has issued a set of priority actions to all the State Government based on thr goals,targets and priorities of Sendai Framework 2015-2030.Government of india,during AMCDRR,2016,has extended the grant of US$1 milion to UNISDR towards effective implementation of the Sendai Framework for Disaster Risk RedUction in the Asian region.</p>
<p>Thirdly, in line with Sendai priority 4, National Disaster Response Force (NDRF) is strengthened, both in terms of state-of-the-art training and equipment to as to further empower it as a professional disaster response force. Besides, Government of India has approved the creation of National Disaster Response Reserve (NDRR) through a revolving fund of Its. 250 crore to be operated by the National Disaster Response Force.</p>
<p><strong>New Delhi Declaration</strong></p>
<p>The &#39;New Delhi Declaration&#39; is a political statement spelling out the commitment of participating governments towards preventing and reducing disaster risk, and strengthening the resilience of communities, nations in the Asian region. Recognising the need to accelerate the implementation of global frameworks, it commits to a people-centred and whole-of-society approach towards DRR. It also emphasises the need to enhance the capacity of communities and ensure participation of all stakeholder groups towards achieving resilience.</p>
<p><strong>Asian Regional Plan</strong></p>
<p>The &#39;Asian Regional Plan for Implementation of the Sendai Framework&#39; focuses on the &#39;How to&#39; reduce disaster risk at national and local levels. It has arrived at a longer term road map of cooperation and collaboration, spanning the 15-year horizon of the Sendai Framework, as well as a two-year action plan to further disaster risk reduction with specific, actionable activities.</p>
<p>Fourthly, the government expressed keenness to share India&#39;s expertise and help other countries in disaster response as it did during Japan Earthquake in 2011 and Nepal earthquake of 2015. The government of India is making consistent efforts to promote regional cooperation by hosting the SAARC Disaster Managenient Centre to reduce disaster risks in the region and promoting knowledge sharing among the SAARC countries. SAARC Management Exercise (SAADMEX) 2015 held in Delhi provided ideal platform for sharing the government&#39;s ideas and experience and reaffirmed its commitment to strengthen the institutional mechanism of regional cooperation on disaster response among the member countries.</p></div>
