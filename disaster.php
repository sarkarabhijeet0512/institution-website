<p><strong>Chemical Disasters and Biological Disasters</strong>&nbsp;</p>
<p><strong>Chemical Disasters: Prevention and Response .</strong></p>
<p>Chemical disasters may arise at any stage of the plant/process life cycle such as commissioning, storage, manufacturing, maintenance, disposal and transportation etc. The loss of contaminant of hazardous chemicals can lead to fire, explosions, toxic release or combination of them. At a time when the population density is high and more and more residential colonies are brewing up around the industries, a major mishap at a chemical plant can spill catastrophe to the people and environment. Besides fire and explosion hazard, the release of toxic chemicals can cause irreparable damage that can last long to people as well as to the environment. The Ministry of Environment and Forest (MoEF) is the nodal Ministry for the management of chemical disasters and National Disaster Management Authority (NDMA) has prepared guidelines to direct ministries, department and state authorities for the preparation of disaster management plans. Since the Bhopal gas tragedy in 1984 there has been&#39; a paradigm shift in the government&#39;s approach to safety in industries from a reactive to proactive safety culture.</p>
<p><br></p></div>
  <div class="menu-content Biological"><p><strong>Causative factors</strong></p>
<p>Ageing of process plants and inadequate steps to pace with modern technologies in Indian chemical industry has increased vulnerability to chemical disasters. Fire, explosion, toxic release and combinations of all can occur during transport, storage and processing At to number of reasons such as temperature and pressure deviations from set limits, runaway reactions, mixing of incompatible materials, catastrophic rupture of reactors, storage vessels, pipelines,leaks, failure of hardware systems, inadequate mixing or agitation failures, improper design of reactor vents, inadequate process hazard analysis etc.</p>
<p>Organic solvents are the most common source of fires and explosions in the chemical industry. A majority of the industrial accidents occur due to human error as a result of non-compliance of Standard Operating Procedures (SOPS) that have been put into place by the company.</p>
<p>Another common cause that results in chemical disaster is the improper maintenance of equipment. Regular maintenance at scheduled intervals following the manufacturer&#39;s recommendations is important for ensuring that the equipment runs smoothly and safely. When a piece of equipment is not properly maintained, it can malfunction and ultimately fail resulting in a catastrophic explosion.</p>
<p><strong>Prevention and Response</strong></p>
<ul>
    <li>Role of Industry: As far as chemical accidents are concerned, prevention is the best proactive approach rather than the reactive methods to chemical disasters. Each of the following stakeholders has a role and responsibility.</li>
    <ul>
<p><strong>&nbsp;Identification of hazardous activities:</strong> A good knowledge about the safety aspects of the industrial operations would enable the prevention and control of accidents.</p>
<p><strong>Maintenance of the plant facility and equipment:</strong> Proper maintenance of all the equipment and machinery need to be carried out at regular intervals. Regular site safety and health inspection needs to be carried out to ensure that the plant facility is safe and all the equipment&#39;s are operating in the intended method.</p>
<p>&mdash; <strong>Installation of vapour/gas detection system:</strong> Installation of gas/vapour detection system with alarms to detect leak even at micro levels would ensure that the leaks are attended at the early stages. Compliance with existing rules and regulations: Various rules and regulations of the state and centre should be strictly adhered for a sustainable and safe process.</p>
<p>&nbsp; &nbsp; &nbsp;<strong>Development of human resource management:</strong>Human resource management is complex and requires a constant monitoring and behaviour based safety program should be implemented.</p>
<p>&nbsp;</p>
<p>&mdash;&gt; <strong>Emergency preparedness:</strong> The industry must have a good emergency response team that can react swiftly to mitigate propagating disasters.</p>
</ul>
    <li>&nbsp;Role of Government</li>
    <ul>
<p><i class="fa fa-hand-o-right"></i><strong>Setting up of accident investigation board and chemical accident database:</strong> An accident investigation board in similar lines of chemical safety board of USA can be set up in India to investigate the. chemical disaster and bring out guidelines based on the lessons learnt in each incident would be helpful to prevent its reoccurrence.</p>
<p><i class="fa fa-hand-o-right"></i><strong>Awareness. Campaigns:</strong> The government can provide awareness regarding the hazards arising arise out a chemical disaster to the workers as well as the public.</p>
<p><i class="fa fa-hand-o-right"></i><strong>Research and development:&nbsp;</strong>initiative to newer technologies that can minimise the toxicity of the byproducts of chemical industries can be carried out by the government. Offsite Emergency Planning: The purpose of offsite emergency plan is to ensure that the local authority adequately discharges his duty to minitnize the consequences of major accident to people and environment in MAH located sites.</p>
<p><i class="fa fa-hand-o-right"></i><strong>Role of Public:</strong> A general awareness of the risk associated with any chemical accident would help in reducing the outcome of the accident. A mutual aid group can be setup to organise the general public in case of any disaster and provide training and awareness about the potential actions to be taken in case of any chemical leakage.</p>
</ul>
</ul>
  <div class="menu-content Plan"><p><strong>Biological Disasters: Causes and Way Forward&nbsp;</strong></p>
<p>Biological disasters define the devastating effects caused by an enormous spread of a certain kind of living organism&mdash;that may spread a disease, virus, or infestations of plant, animal or insect life on an epidemic or pandemic level. Cholera and influenza H1N I (Swine flu) outbreaks are examples of biological disasters. Epidemic-level biological disasters affect large numbers of people within a given community or area, whereas pandemic-level biological disasters effect a much larger region, sometimes spanning entire continents or the globe. Cholera is an epidemic level biological disaster, while swine flu is a pandemic. Other epidemic examples include ebola, dengue fever, malaria and the measles.</p>
<p>Biological hazards, also known as biohazards, refer to biological substances or organic matters produced by parasites, viruses, bacteria, fungi and protein that pose a threat to the health of living organisms, primarily that of humans. This can include medical waste or samples of a microorganism, virus or toxin (from a biological source)</p>
<p>that can affect human health. It can also include substances harmful to other animals.</p>
