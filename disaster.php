<p><strong>Tsunami&nbsp;</strong></p>
<p>Tsunami is derived from the Japanese word &#39;tsu&#39; meaning harbour and &#39;narni&#39; meaning wave. 4 tsunaini is a series of waves caused by a rapid displacement of a body of water (ocein, lake). Tsunamis are waves generated by earthquakes,volcanic eruptions, or underwater landslides and can reach 15km or more height devastating costal communications.The waves are characterised by a very long wavelength and their amplitude is much smaller offshore. The impact in costal areas can be very destructive as the waves advance inland and can extend over thousands of kilometers. Triggers of a tsunami can be: earthquakes, volcanic eruption mass movements, meteorite impacts or underwater explosions. A killer tsunami hit It south eastern Countries of Asia on the 26th December 2004 killing more than 1,50,000 precious lives. The emotional, economical and ecological toll of the disaster can&#39;t be calculated. Many village have lost an entire generation.</p>
<p><strong>Advance Warnings about Tsunamis</strong></p>
<p>The best way of escaping the fury of the killing tsunami waves is to give advance warning of impending tsunamis to the vulnerable communities. Following few tips will help the common man to apprehend the danger of tsunamis:</p>
<ul>
    <li>If there is a major undersea earthquake, beware of the danger because tsunamis could reach the coast in a few minutes.</li>
    <li>Shaking of ground is a sufficient warning for the people living inthe coastal areas about the impending tsunami.</li>
    <li>A perceptible rapid rise or fall in coastal water can also be taken as a forewarning an approaching tsunami.</li>
    <li>Coastal areas less than eight meters above sea level run the greatest risk of an attack by tsunamis. They should be immediately evacuated if there is danger of an approaching tsunami.</li>
</ul>
