                    <div class="card-body" style="text-align: justify;"><h3>NATIONAL INCOME</h3><hr>
    <h6>Introduction</h6>

            National income is defined as the value of all final goods and services produced by the residents of a country, whether operating within the domestic territory of the country, or outside, in a year. National income is thus, a momentary expression of the current achievements of they people of expression of the current achievements of the people of a country expressed through their production activities.

            <b>National product is stock and quantitative concept.</b><br>

                        <b>National Product=Price * Quantity</b><br>

                                                   <b>NP= PQ</b><br>

            National income is the sum of return of all the resources involved in any economic activities. It is qualitative and dynamic concept and related to flow.

            <b>National income = Wage + Rent + Interest + Profit</b><br>

            National income can be defined in many ways. In simplest terms, it can be described as the income of all the people of a country during a year.<br>

            It is also defined as the money value of all the final goods and services product in an economy over a specified period of time. It indicates the performance of an economy in terms of production of goods and services. It is used as a measure of economic growth and reflects the productive power of an economy to turn out goods and services for the satisfaction of human wants.<br>

            <b>Gross Domestic Product (GDP):</b> GDP is monetary value of final goods and services which are produced by general residents of country in own physical boundary.<br>

            <b>GDP = C + I + G</b><br>

            <b>Here, c = Consumption, I = Investment</b><br>

            <b>G = Government Expenditure</b><br>

            <b>Net Domestic Product (NDP): It is the difference between GDP and depreciation.</b><br>

            <b>NDP = GDP – Depreciation, NDP = C + I + G – D</b><br>

            <b>Depreciation:</b> It refers to the reduction in the value of capital assets, (e.g. machinery building, equipment, etc). Due to normal wear and tear and technological obsolescence. Depreciation is also called consumption of fixed capital and capital consumption. Net value is obtained by reducing the values of depreciation from the value.<br>

            <b>Net value = Gross value – Depreciation</b>

            <b>Gross National Product (GNP):</b> GNP is the sum of values of all final goods and services which is produced by citizen of a country in financial year and net earned income from abroad.<br>

            <b>GNP = C + I + G + (X-M)</b><br>

            <b>X – Income earned from export, M – Income expenses on import</b><br>

            <b>Basic difference in GDP and GNP:</b> In case of GDP of net income earned from abroad excluded but in case of GNP it is included. In India only two years 1972-73 and 1976-77 GNP was more than GDP. These two years are to be called golden years of Indian economy.<br>

            <b>Net National Product (NNP):</b> It is difference between GNP and Depreciation.<br>

<b>NNP = GNP – Depreciation</b>
<br><br>
<b>Real income:</b> Real income is national income expressed in terms of general level of prices of a particular year taken as base. National income is the value of goods and services produced as expressed in terms of money at current prices. But it does not indicate the real state of the economy. It is possible that the net national product of goods and services this year might have been less than that of the last year, but owing to an increase in prices, the NNP might be higher this year. On the contrary, it is also possible that NNP might have increased but the price level might have fallen, as a result of which national income would appear to be less than that of the last year. In both the situations, the national income does not depict the real state of the country. To rectify such as a mistake, the concept of real income has been evolved.<br>

            Factor Cost versus Market Price: Factor cost (FC) refers to the payments made to the factors of production (i.e. labour, land, capital, and entrepreneurship) of goods and services, while Market Price (MP) refers to the market value of the goods and services produced. Market price of the goods and services is arrived at by adding Net Indirect Taxes (i.e. Indirect Taxes – Subsidies) in the cost of production (FC).<br>

            <b>MP = FC +== Net Indirect Taxes</b><br>

            <b>e.g. GDP (MP) = GDP (FC) + Indirect taxes – Subsidies</b><br>

            <b>or, GNP MP = GNP FC + Indirect Taxes – Subsidies</b><br>

            Current Price versus Constant Price: National income at current prices shows the value of the goods and services at the market prices prevailing in the current year (i.e. the same year in which they were produced), while national income at constant prices shows the value of the goods and services at the base year (1993-94) prices. National income at current prices is called ‘Nominal national Income’ while national income at constant prices is called Real National Income, as it offers the effects of inflation.<br>

            <b>Deflation of GDP:</b> Conversion of Nominal GDP into Real GDP by adjusting it for price level changes is called deflation of GDP<br>

<b>Real GDP Constant Price = GDP Current prices * 100/Price Index Number</b><br>

            For example, the value of nominal GDP was 28.38 lakh, crore in the financial year 2004-05 and the (mid-year) value of Wholesale Price Index (WPI) was 185.57, then the value of Real GDP can be calculated as follows:<br><br>

<b>Real GDP = 28.38 lakh crore * 100 /185.57 = 15.29 lakh crore</b><br><br>

            <b>Capital Output Ratio:</b> The required capital for production of one unit is called capital output ratio.<br><br>

            <b>Capital Output Rato = Total capital / Total production</b><br><br>

           <b> C.O.R = Total capital/ Total Production</b><br><br>

            <b>Average Capital Output Ratio (A.C.O.R)</b><br><br>

            <b>Incremental Capital Output Ratio (I.C.O.R)</b><br><br>

            I.C.O.R = Increase in capital / Increase in production
