<b>Task Force on Implementation of FRBM Act</b><br>

      Following the enactment of FRBM Act, Government constituted a Task force headed by Dr. Vijay Kelkar for drawing up the medium-term framework for fiscal policies to achieve the FRBM targets. The Task Force was also asked to formulate annual targets indicating the path of adjustment and required policy measures. The Task Force submitted its report in July 2004. The Task Force recommended a path of fiscal adjustment that this front loaded and mainly revenue-led, with complementary reform efforts on revenue expenditure and enhanced capital expenditure to counteract the possible contractionary effects of fiscal correction.<br>

      The Task Force estimated that under the reforms scenario recommended by it, tax-GDP ratio of the Centre would improve from 9.2% in 2003-04 (RE) to 13.2% in 2008-09. Total expenditure is estimated to come down 14.3% of GDP by 2008-09 from 15.4% of GDP in 2003-04 (RE). A revenue surplus of 0.2% of GDP is estimated in 2008=-09. Fiscal deficit is estimated to come down from 4.8% of GDP in 2003-04 to 2.8% of GDP in 2008-09.

                      
