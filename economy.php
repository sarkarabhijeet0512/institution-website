  <div class="menu-content economic-reforms"><h3>ECONOMIC REFORMS</h3><hr>

            The entire period of economic planning in India can be divided into two different phases- the first phase spreading over the plan period from1950-51 to 1990, while the second phase commences from 1991. The first phase laid down the basic objectives and strategies of development while the second phase showed a significant shift in the original objectives and strategies of planning leading to economic reforms.
            <br><br>
<b>First Generation Reforms:</b>

            The Economic Reforms of 1991 or the New Economic Policy is a complete reversal of the previous policies. These economic reforms were based on three premises Liberalisation, Privatisation and Globalisation (LPG). These reforms seek to free the Indian economy from undue controls, give greater freedom to the more efficient private sector and integrate the Indian economy with the world economy. These reforms aimed at putting an end to the government’s Licence, Quota and Permit (LOP) raj by initiating the LPG mantra.
            <br><br>

            <b>Economic liberalisation:</b> It implies providing greater freedom to economic agents to take their own economic decisions. It generally means leaving the economy to free market forces with least government interference.
            <br><br>

<b>Salient Features of the New Economic Policy:</b>


            The economic reforms (First Generation Reforms) introduced the following policy changes:
            <ul>
            <li><b>Delicensing:</b> Industrial licensing system has been abolished for most of the industries.</li>

            <li><b>Dereservation:</b> the industries which were exclusively reserved for the public sector were deserved expect for a few.</li>

            <li><b>Deregulation:</b> The industries laws like Monopolies and Restrictive Trade Practices (MRTP) Act were amended to ease restrictions on economic units.</li>

           <li><b> Disinvestment:</b> The government proposed to sell the government equity (share capital) in certain Public Sector Units (PSUs) to the private sector.</li>

            <li><b>Easing of trade barriers:</b> The restrictions on imports and exports were removed and tariffs were lowered.</li>

            <li><b>Encouragement to foreign capital and technology:</b> Restriction on the operations of the Multinational Corporations (MNCs) were removed and Foreign Exchange Regulation Act (FERA) was relaxed to allow free inflow of foreign investments.</li>

            <li><b>Taxation reforms:</b> The tax rates and tax slabs were rationalised, taxation system was simplified and the loopholes in the administration were plugged.</li>

            <li><b>Fiscal reforms:</b> The government proposed to reduce the non-plan expenditure in order to reduce budgetary deficits.</li>

            <li><b>Banking reforms:</b> The interest structure was deregulated and greater autonomy was provided to the banks.</li>
</ul>
<b>Second Generation Reforms:</b>
<br>

            Economic reforms programme that began under the Rao-Manmohan Model of the economic reforms in 1991 aimed at delicencing and decontrolling vital sectors of the economy. It involved abolishing industrial licensing, opening of trade, disinvestment and privatisation, tax reforms, private financing infrastructure like power and telecom, etc. The 1991 policy had two parts: firstly to extricate the economy out of the deep macroeconomic crisis by undertaking ‘stabilisation measures’ and secondly to bring about certain structural changes’ in the economy. The first step was mainly a short-term measure while the second was generally of the nature of medium to long-term and designed to free the supply side of the economy.
            <br><br>
            These reforms have, however, left the factor markets such as labour market, capital market, natural resources etc. mostly untouched. The aim of the second generation reforms is to bring reform into these areas. The name, ‘second generation’ does not imply that we have successfully completed the first generation of reforms and should move to the next higher level.
            <br><br>
            In fact, the two generations of reforms are overlapping. Such reforms are complementary as well as supplementary to the First Generation Reforms. So the second generation reforms have two dimension, i.e. to carry forward first generation reforms and to initiate reforms in new areas.
            <br>
<b>Carry Forward First Generation Reforms</b>
<ul>
     <li><b>Banking sector reforms:</b> Such reforms should extended to encompass the entire financial sector. Reforms should be initiated in money and capital markets in the form of dematerialisation, corporation and demutualisation of stock exchanges.</li>

        <li><b>Taxation in Free India:</b> CENVAT should be converted into full-fledged VAT. Reforms should also be extended to the fiscal policy like reduction of the public expenditure borrowing, downsizing government staff etc.</li>
</ul>
<b>Planning in Free India</b>
<br>

           <p> The Planning Commission was set up by a Resolution of the Government of India in March 1950 in pursuance of declared objectives of the Government to promote a rapid rise in the standard of living of the people by efficient exploitation of the resources of the country, increasing production and offering opportunities to all for employment in the services of the community.</p>

           <p> The Planning Commission was charged with the responsibility of making assessment of all resources of the country, augmenting deficient resources, formulating plans for the most effective and balanced utilisation of resources and determining priorities. Jawaharlal Nehru was the first Chairman of the Planning Commission.</p>

           <p> The first five year plan was launched in 1951 and two subsequent five-year plans were formulated till 1965, when there was a break because of the Indo-Pakistan conflict. Two successive years of drought, devaluation of the currency, a general rise in prices and erosion of resources disrupted the planning process and after three Annual Plans between 1966 and 1969, the fourth five-year plan was started in 1969.</p>

           <p> The eighth plan could not take off in 1990 due to the fast changing political situation at the centre and the years 1990-91 and 1991-92 were treated as Annual Plans. The eight plan was finally launched in 1992 after the initiation of structural adjustment policies.</p>

           <p> For the first eight plans the emphasis was on a growing public sector with massive investments in basic and heavy industries, but since the launch of the Ninth Plan in 1997, the emphasis on the public sector has become less pronounced and the current thinking on planning in the country, in general, is that it should increasingly be of an indicative nature.</p>
