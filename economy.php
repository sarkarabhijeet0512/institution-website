<b>Goals of Planning</b><br>

            Planning in India derives its objectives from the Directive Principles of State Policy enshrined in the constitution. Goals and objectives of planning can be summarised into two categories, i.e. short-term and long-term goals. Short-term goals of a plan refers to the immediate and most important targets which a particular plan endeavours to achieve. Such goals are called focus of plan as they reflect the strategy of a particular plan. On the other hand, long-term objectives of planning are the general guiding principles of the planning process and reflects the aspirations of a welfare state. The long-term objectives of planning in India have been more or less same in almost all the five-year plans and are as follows:
<ul>

        <li>To accelerate the pace of economic growth in order to raise the standard of living of the people. That is to achieve higher levels of national income and per capita income.</li>

        <li>To achieve self-reliance in respect of the production of food grains, development of basic industries, technological know-how and foreign exchange earnings. It aims to eliminate vulnerability of the Indian economy to international pressures and disturbances.</li>

        <li>To ensure social justice to the people by eliminating poverty, unemployment and inequalities of income and wealth.</li>

        <li>To modernise the economy by bringing about structural and institutional changes in the framework of economic activity.</li>
</ul>
<b>Self-sufficiency and Self-reliance:</b>

            Self-sufficiency implies that a country produces all commodities it requires within the country without having to import them, while self-reliance implies that a country has sufficient foreign exchange reserves to pay for its imports.<br>


