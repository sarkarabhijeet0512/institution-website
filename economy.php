            <b>The certificate of deposit market:</b> A Certificate of Deposit (CD) is a   certificate issued by a bank to depositors of funds that remain on deposit at the bank for a specified period. Thus, CDS are tradable in the short-term money markets.RBI introduced CDs with the objective of widening the range of money market instruments and to provide investors greater flexibility in the deployment of their short-term surplus funds.
<br>
