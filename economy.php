                    <div class="card-body" style="text-align: justify;"><h3>BUDGETARY DEFLICITS</h3><hr>
<ul>
        <li><b>Budget deficit:</b> The budget deficit is the difference between the total expenditure and total receipts, other than internal capital receipts. It has to be financed through the sale of 91 days and hoc treasury bills to the RBI and drawing down of cash balances.

<b>Budget Deficit = Total Expenditure – Total Receipts = 91-day ad hoc treasury bills + cash withdrawals</b>

The issue of 91 days ad hoc treasury bills leads to the monetisation of the      deficit, (i.e. printing of new money), so government abandoned the practice of issuing such securities to the RBI since 1997. Instead, the government had initiated a scheme of Ways and Means Advances (WMA). Thus, the concept of budget deficit has become redundant.</li>

      <li> <b>Ways and Means Advances (WMA):</b> Under this Scheme, the RBI provides facilities for temporary accommodation of the financial needs of the government up to a ceiling. Now, yearly limits are divided into six months each, decided mutually between the government and the RBI at the beginning of a year. Thus, the WMA is purely a mechanism to bridge the temporary mismatch between the receipts and expenditure of the government.</li>

       <li><b>Revenue Deficit:</b> Revenue Deficit (RD) is the excess of revenue expenditure over revenue receipts, i.e. R.D. Revenue expenditure – Revenue receipts (Revenue deficit shows the extent of reduction of assets or increment in liabilities of the government).</li>

<b>In simple terms, it can be understood as the excess of current consumption expenditure of the government (which does not yield any future benefit) over its current income. So, it is regarded as the worst kind of deficit from the perspective of economic welfare.</b>

<b>Effective Revenue deficit is a new term introduced in the Union Budget 2011-12. While revenue deficit is the difference between revenue receipts and revenue expenditure, the present accounting system includes all grants from the Union Government to the state governments/Union territories/other bodies as revenue expenditure, even if they are used to create assets. Such assets created by the sub-national governments/bodies are owned by them and not by the Union Government. Nevertheless they do result in the creation of durable assets.</b>

       <li><b>Fiscal deficit:</b> Fiscal deficit is the excess of total expenditure of the government over the total receipts other than borrowings, i.e.,</li>

<b>F.D = Total Expenditure – Total receipts other than borrowings</b>

In other words, it can be defined as the difference between the total expenditure on one hand and the revenue receipts plus non-debt creating capital receipts on the other, i.e.,

<b>F. D = (Total Expenditure) – (Revenue Receipts + Non-debt creating capital receipts)</b>

Fiscal deficit shows the extent of increment in public debt, (i.e., total borrowings) during a fiscal year. It is the most comprehensive measure of deficit as it depicts the total resource gap of the government budget.

<b>Crowding out:</b> It refers to the reduction in private investment as a result of high interest rates due to excessive government borrowings.

       <li><b> Primary deficit:</b> Primary deficit (PD) is the difference between the fiscal deficit and interest payments, i.e.,</li>

<b>PD = Fiscal Deficit – Interest Payments.</b>

It reflects the status of the current operations of the government. That is, the extent to which current government policy is adding to future burdens stemming from past policy.

        <li><b>Monetised deficit:</b> It is also called Financing. It is defined as the net increase in RBI credit to the government during a fiscal year. Government borrowings from RBI directly add to high powered money i.e., printing of new money. It deficits the increase in the high powered money and leads to a multiple expansion in money supply, which generates inflation. Monetised Deficit is only a part of fiscal deficit as it takes into account borrowings of the government only from the RBI.</li>

<b>Public Expenditure</b>
<ul>
      Public expenditure refers to the total expenses incurred by the government on various heads. Public expenditure can be classified as revenue or capital development or non-developmental and plan or non-plan.

        <li><b>Plan expenditure and non-plan expenditure:</b> Plan expenditure refers to that expenditure whose provision is made under the annual budgets for those programmes and projects which are mentioned under the five-year plans. It includes expenditure on energy, social services, communication, transportation, agriculture, rural development, etc.</li>

       <li> Non-plan expenditure refers to that expenditure whose provision is made under the annual budgets on the items not included in the five-year plans. Simply, any expenditure other than plan expenditure is treated as non-plan expenditure. It includes expenditure on defence, interest payments, subsidies, expenditure on tax collection etc. (Note: Government Scrapped Distinction Between Plan & Non-Plan Expenditures in Budget 2016-17)</li>

        <li><b>Developmental and non-developmental expenditure:</b> Development expenditure relates to the growth and development activities of the government. This includes expenditure on education, health, industry, agriculture, transport, roads, etc.</li>

      Non-developmental expenditure is incurred on those items which do not directly promote growth and development economy. It includes expenditure on defence, interest payments, tax collections charges, civil administration, etc.<br>
      <br>
</ul>
<b>Public Debt</b>

      Public debt refers to the total accumulated borrowings of the government. Public debt includes internal debt comprising borrowings inside the country like market loans, compensation and other bonds, treasury bills, special securities issued to the RBI as well as non-negotiable non-interest bearing rupee securities issued to the international financial institutions. External debt comprises of loans from foreign countries and from international financial institutions like WB, IMF, ADB, etc.

      The other liabilities’ include outstanding against various small saving schemes, provident funds, securities issued to IDBI, UTI and nationalised bank, deposits schemes, reserve funds and deposits. While ‘public debt’                                     obligations are raise on the security of consolidated fund of India and are repayable out of it, ‘other liabilities’ are payable out of public Account Fund of India.
</ul>
                      
