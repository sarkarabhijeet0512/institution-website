<b>Alagh Committee 91979):</b> In 1979, a task force constituted by the Planning Commission for the purpose of poverty estimation, chaired by Y.K Alagh, constructed a poverty line for rural and urban areas on the basis of nutritional requirements. Poverty estimates for subsequent years were to be calculated by adjusting the price level for inflation.<br>


      <b>Lakdawala Committee (1993):</b> In 1993, an expert group constituted to review methodology for poverty estimation, chaired by D.T. Lakdawala, made the following suggestions:
<ul>
      <li>Consumption and expenditure should be calculated based on calorie consumption as earlier;</li>

        <li>State specific poverty lines should be constructed and these should be updated using the Consumer Price Index of Industrial workers (CPI-IW) in urban areas and Consumer Price Index of agricultural labour(CPI-AL) in rural areas; and</li>

        <li>Discontinuation of <b style="color: red;">‘scaling’</b> of poverty estimates based on National Accounts Statistics. The assumes that the basket of goods and services used to calculate CPI-IW and CPI-AL reflect the consumption patterns of the poor.</li>
<br>

      <li><b>Tendulkar Comittee (2009):</b> In 2005, another expert group to review methodology for poverty estimation, chaired by Suresh Tendulkar was constituted by the Planning Commission to address the following three shortcomings of the previous methods:</li>

        <li>Consumption patterns were linked to the 1973-74Poverty Line Baskets (PLBs) of goods and services, whereas there were significant changes in the consumption patterns of the poor since that time, which were not reflected in the poverty estimates;</li>

        <li>There were issues with the adjustment of prices for inflation, both spatially (across regions) and temporally (across time); and</li>

        <li>Earlier poverty lines assumed that health and education would be provided by the State and formulated poverty lines accordingly.</li>

        <li><b>It recommended four major changes:</b></li>
          <ol type="a">
        <li>A shift away from calorie consumption based poverty estimation;</li>

        <li>A uniform Poverty Line Basket (PLB) across rural and urban India;</li>

        <li>A change in the price adjustment procedure to correct spatial and temporal issues with price adjustment; and</li>

        <li>Incorporation of private expenditure on health and education while estimating poverty.</li>
</ol>
        <li>The Committee recommended using Mixed Reference Period (MRP) based estimates, as opposed to Uniform Reference Period (URP) based estimates that were used in earlier methods for estimating poverty.</li>

