NNPfc (or National Income) = domestic Factor Incomes + Net Factor Income earned From Abroad NNPfc = GNPfc – Depreciation
<br>
National income can also be found out by deducting depreciation from gross product at factor cost.
<br>
New Series Estimates of National income, Consumption Expenditure, Saving and Capital Formation (Base Year 2011-12)</li>

       <li> The Ministry of Statistics & Programme Implementation has released the new series of national accounts, revising the base year from 2004-05 to 2011-12 base year of national accounts was last revised in January 2010.</li>

       <li> Base year revisions differ from annual revisions in National Account primarily because of nature of changes. In annual revisions, changes are made only on the basis of updated data becoming available without making any changes in the conceptual framework or using any new data source, to ensure strict comparison over years. In case of base years revisions, apart from a shift in the reference year for measuring the real growth, conceptual changes, as recommended by the international guidelines are incorporated. Further, statistical changes like revisions, in the methodology of compilation, adoption of latest classification systems and inclusion of new and recent data sources are also made. Changes are also made in the presentation of estimates to improve ease of understanding for analysis and facilitate international comparability.</li>

