      The Balance of Payments (BoP record the transactions in goods, services and assets between residents of a country with the rest of the world. There are two main accounts in the BoP- the current account and the capital account. The current account records exports and imports in goods and services and transfer payments. Trade in services denoted as invisible trade (because they are not seen to cross national borders) includes both factor income ( payment for inputs investment income, that is, the interest, profits and dividends on our assets abroad minus the income foreigners earn on assets they own in India) and non-factor income (shipping, banking, insurance, tourism, software services, etc.).
      <br>
      Transfer payments are receipts which the residents of a country receive ‘for free’, without having to make any present or future payments in return. They consist of remittances, gifts and grants. They could be official or private. The balance of exports and imports of goods is referred to as the trade balance.<br>

      Adding trade in services and net transfers to the trade balance, we get the current account balance. The capital account records all international purchases and sales of assets such as money, stocks, bonds, etc. we note that any transaction resulting in a payment to foreigners is entered as a debit and is given a negative sign. Any transaction resulting in a receipt from foreigners is entered as a credit and is given a positive sign.<br>

