<b>Meaning of Economic Planning</b>
<br>

            Economic Planning means the allocation of limited resources among different uses in such a way as to bring about the maximum welfare of the people. Economic planning consists of two basic elements. One is the determination of objectives and second is the provision of means for the achievement of those objectives. The detailed scheme is called economic Plan. The planning may either be centralised or decentralised depending upon the national requirements and degree of involvement of different sectors. The centralised planning refers to complete central control over major parts of the economic activities. Some of the decisions are related to investment in different sectors of economy, fixation of price structure, rate of wages and the amount of production of a particular commodity.
            <br>
<b>Types of Planning:</b>

            Planning can be categorised into various categories on the basis of the following.<br>

<b>Role of the Government:</b><br>
<ul>
        <li>Imperative Planning: It is also known as directive/totalitarian/authoritarian planning or planning by direction command. Under imperative planning, the central planning authority decides about each and every aspect of the economy. The target sets the processes delineated to achieve them are to be strictly followed. This type of planning is generally practiced in the socialist economies.</li>

        <li>Planning by inducement: Under such kind of planning, state regulates the private sector through the use of various incentives and disincentives (i.e. fiscal, monetary and industrial policy measures), so that the private sector may cooperate in fulfilling the targets and priorities of the plan. This kind of planning is generally practices in mixed economies.</li>

       <li> Indicative planning: It is also known as soft or facilitatory planning. Under it, state sets broad parameters and goals for the economy and endeavours to achieve them through persuasion and facilitation. The state provides all types of facilitates to the private sector and indicates the areas in which it can help in implementing the plan. It is based on French model of planning and generally practiced in capitalistic economics. It has been pursued in India since the Eight Five Year Plan.</li>
</ul>
<b>Extent of People’s Participation</b>
<ul>
       <li> Centralised planning: Under centralised planning there is a central planning authority which formulates the plan, fix objectives, target and priorities for every sector of the economy. There is a central control and all economic activities are directed in accordance with a single plan.</li>

        <li>Decentralised planning: It is also known as democratic planning, planning from below and grass root planning. A decentralised plan is characterised by the widest possible consultations with the various state governments and the private sector at the stage of formulation and implementation of the plan. The central plan incorporates plans under the central schemes and plans for the states as well as the district and village level plans.</li>
</ul>
<b>Economic systems:</b>

            The nature and scope of planning is largely determined by the nature of the economic system in which it is practices. Economies can be classified into the following categories:
            <ul>
       <li> <b>Socialist economy:</b> It is characterised by the existence of public enterprise or state ownership of capital in all the important spheres of the economic activity. All important economic decisions are undertaken by the planning authority. Such economies generally pursue planning by direction or imperative planning.</li>

        <li><b>Capitalistic economy:</b> It is characterised by the existence of private enterprise and ownership of all important means of production. Resources are allocated by the operation of the price mechanism by which buyers and sellers determine what goods and services will be bought and sold in the market, in what quantities and what prices. Such economies generally pursue indicative planning, as the role of planning authority and government is minimal.</li>

        <li><b>Mixed economy:</b> It is characterised by the presence of the features of socialism as well as that of capitalism i.e., public sector co-exist with the private sector-Resources are allocated on the basis of demand and supply with selective government intervention. Such economic generally pursue planning by inducement i.e., government regulate the activities of the private sector through persuasive measures like tax holidays, subsidies etc.</li>

        <li><b>Community economy:</b> It is an extreme form of a socialist economy in which all means of production are owned and operated by the state and private property is not allowed. It is modelled after the notion of Karl marx i.e., “from each according to his ability, to each according to his need”. Socialism is said to be the preparatory but it has not yet established in any of the socialist country.</li>
</ul>

<b>Features of Indian Planning:</b><br>

            The important characteristics of the economic planning in India are as follows:
<ul>
       <li> Indicated Planning: Indian planning has assumed indicative character since the Eighth Five-Year Plan (1992-97).</li>

&rarr; <b> Core Plan:</b> The concept of core plan enables the planning authority in formulating effective plans through realistic assessment of the resources. Under a core plan, the Planning Commission determines the expenditure head of state annual plans.<br>

&rarr; <b>Democratic socialism:</b> It connotes to the adoption of the principle of socialism (i.e., classless society) within a democratic (i.e., ensuring individual liberty) framework. It was Nehru’s vision of independent India in which all have equal opportunities in education, health, employment etc.<br>

&rarr; <b> Socialistic pattern of society:</b> It connotes to such a mixed economic system in which there are no economic and social inequalities in the distribution of income and wealth in a country. State owned means of production are used to promote social welfare and privately owned means of production are effectively regulated by the state through various instruments on the economic policy. It was adopted as one of the goals of the economic planning in India in the Second Five Year Plan.<br>

       <li><b> Decentralised and democratic planning:</b> People are involved at various stages of preparation and implementation of the plans.</li>

        <li><b>Comprehensive planning:</b> Planning process encompasses each and every sector of the Indian economy.</li>

        <li><b>Physical and financial planning:</b> Resources are assessed and allocated in both real and monetary terms.</li>

        <li><b>Perspective planning:</b> The five-year plans sets objectives and targets beyond the plan period, e.g. the 10th plan sets certain targets to be accomplished by 2012.</li>
</ul>
                      
