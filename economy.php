                      <h3>BASIC CONCEPT OF ECONOMY</h3><hr>

<h6>ECONOMIC DEVELOPMENT AND ECONOMIC GROWTH</h6>
  

            Economic growth is related to a quantitative sustained increase in the country’s per capita output or income accompanied by expansion in its labour force, consumption, capital and volume of trade. On the other hand, economic development is a wider concept than economic growth. It is related to qualitative changes in economic wants, goods, incentives, institutions, productivity and knowledge or the “upward movement of the entire social system”. It describes the underlying determinants of growth such as technological and structural changes. In fact, economic development embraces both growth such as technological and structural changes. In fact, economic development embraces both growth and decline. An economy can grow but it may not develop because poverty, unemployment and inequalities may continue to persist due to the absence of technological and structural changes. But it is difficult to imagine development without economic growth.

        <br><br>
            
<h6>MEASUREMENT OF ECONOMIC DEVELOPMENT:</h6>

<ul>

<li><b>GROSS NATIONAL PRODUCT:</b> One of the methods to measure economic development is in terms of an increase in the economy’s real national income over a long period of time. But this is not a satisfactory definition due to the following reasons:</li>

<p>&rarr; “Real national income” refers to the country’s total output of final goods and services in real terms rather than in money terms. Thus price changes will have to be ruled out while calculating real national income. But this is unrealistic because variations in prices are inevitable.</p>

<p>&rarr; This measure fails to take into consideration changes in the growth of population. If a rise in real national income is accompanied by a faster growth in population there will be no economic growth but retardation.</p>

<p>&rarr; The gross National Product (GNP) figure also does not reveal the costs incurred by the society on account of environmental pollution, urbanisation, industrialisation and population growth, It considers natural resources to be free and treats the earth “like a business in liquidation”.</p>

<p>&rarr; Further, it tells us nothing about the distribution of income in the economy.</p>


<li><b>GNP PER CAPITA:</b> The second measure relates to an increase in the per capita real income of the economy over the long period. This indicator of economic growth purports to emphasise that for economic development the rate of increase in real per capita income should be higher than the growth rate of population. But difficulties still remain.</li>

  <p>&rarr;An increase in per capita income may not raise the real standard of living of the masses. It is possible that while per capita real income is increasing, per capita consumption might be falling.</p>

   <p>&rarr;There is another possibility of the masses remaining poor despite an increase in the real GNP per capita if the increased income goes to the few rich instead of going to the many poor.</p>

   <p>&rarr;The real GNP per capita fails to take into account problems associated with basic needs like nutrition, health, sanitation, housing, water and education. The improvement in living standards by providing basic needs cannot be measured by increase in GNP per capita.</p>
 

Despite these limitation, the real GNP per capita is the most widely used measure of economic development.

<li><b>WELFARE:</b> There is also a tendency to measure economic development from the point of view of economic welfare. Economic development is regarded as a process whereby there is an increase in the consumption of goods and services of individuals. This indicator is also not free from limitations.</li>

<li><b>SOCIAL INDICATORS:</b> Dissatisfied with GNP per capita as the measure of economic development, certain economists have tried to measure it in terms of social indicators. Economists include a wide variety of items in social indicators. Some are inputs such as improvements in health in terms of infant mortality rates, sickness rates, etc. Social indicators are often referred to as the basic needs for development. Basic needs focus on alleviation of poverty by providing basic human needs to the poor.
<br>
The merit of social indicators is that they are concerned with ends, being human development. Economic development is a means to these ends. Social indicators tell us how different countries prefer to allocate the GNP among alternatives. Some may prefer to spend more on education and less on hospitals. Moreover, they give an idea about the presence, absence or deficiency of certain basic needs.</li>
</ul>

