                    <div class="card-body" style="text-align: justify;"><h3>TAXATION</h3><HR>

      A tax is a compulsory payment made by a person or a firm to the government without reference to any benefit, the payer may derive from the government.<br>

<b>Classification on the Basis of Method of Taxation</b>
<ul>
      <li><b> Progressive tax:</b> Under such taxation system, tax rate increases with the increase in income. It is based on the ‘ability to pay principle’ of taxation. Their real burden is more on rich and less on poor. It is an effective tool of fiscal policy to reduce inequalities of Income and wealth in a society.</li>

        <li><b>Proportional tax:</b> In the proportional tax system, tax rate remains constant irrespective of the level of income.</li>

        <li><b>Regressive tax:</b> In regressive taxation, tax rate decreases with increase in income. It is reverse of the progressive tax system. Such system of taxation is against the objective of a welfare state as it tends to accentuate income inequalities. So, it is only a theoretical concept, it has not been adopted by any country of the world.</li>

        <li><b>Degressive tax:</b> The degressive tax system is a mixture of proportional as well as progressive taxation system, so it is also called mild progressive tax. Under this system, tax rate increases with increase in income upto a limit than becomes constant.</li>

</ul> 

<b>Classification on the Basis of Imposition</b>
<ul>
       <li><b> Ad-valorem tax:</b> Under this tax system, tax is imposed on the basis of the value of the commodity. Such taxes are equitable in incidents as they conform with the ‘ability to pay principle’ of taxation.</li>

        <li><b>Specific tax:</b> Such taxes are imposed on the basis of specific attributes or qualities of commodities like unit, weight, length, size or volume, e.g. specific excise duty may be levied on the cloth in the length units.</li>
</ul>
<b>Classification on the Basis of form of Tax</b>
<ul>
<b>Direct Taxes</b>
<ul>
        <li>Direct taxes are those taxes in which the impact (levy and incidence (final burden) are on the same person, i.e. the tax is borne by the person on whom it is levied</li>

       <li> Such taxes are imposed on income and wealth of individuals and items</li>

        <li>Such taxes include personal income tax, corporate tax, wealth tax, gift tax, etc.</li>

       <li> Payment of such taxes is compulsory and cannot be escaped.</li>

        <li>Direct taxes are generally progressive, i.e., they tends to reduce economic inequalities.</li>
</ul>
<b>Types of direct taxes</b>
<ul>
        <li><b>Personal Income Tax (PIT):</b> It is levied on the incomes of individuals and unregistered firms.</li>

        <li><b>Corporate Income Tax (CIT):</b> it is levied on the incomes of joint stock companies and registered firms.</li>

        <li><b>Wealth tax:</b> It is imposed on accumulated wealth or property of every individual, Hindu undivided family and closely held companies.</li>

        <li><b>Estate duty:</b> It is imposed on the property of a deceased person which is inherited by his heirs.</li>

        <li><b>Gift tax:</b> It is imposed on the donor of the gift.</li>

        <li><b>Expenditure tax:</b> It was imposed in 1987 on expenditure incurred in hotels where the room charges for a residential accommodation were 400 or more per day per person.</li>

        <li><b>Interest tax:</b> It is levied on the gross interest income of financial institutions.
</li>
</ul>
</ul>
                      
