<b>Indian Money Market:</b>

            Money Market refers to a mechanism whereby on the one hand borrowers manage to obtain short-term loanable funds and on the other; lenders succeed in getting credit worthy borrowers for their money. In any money market, commercial banks are the most important lenders. These banks are, however, not merely the lenders of money, they also create credit. The central banks role is important as the controller of credit. The Indian money market is not an integrated unit. It is broadly divided into two parts, viz. the unorganised and the organised.
<br><br>
<b>Unorganised Sector of the Indian Money Market</b><br>

            Unregulated non-bank financial intermediaries such as fanince companies, chit funds, nidhis, etc. indigenous banlers and moneylenders operate in the unorganised sector of the Indian money market. Lendings of the  moneylender are rather small, but some finance companies, chit funds and indigenous bankers do a considerable amount of business. They pursue banking business on traditional lines. Some of them are:
<br>
            Unregulated non-bank financial intermediaries: Loan companies (also called finance companies) are found in all parts of the country. The exact members of Loan Companies are not known. In addition, a large number of partnership firms and individuals are engaged in lending business. The chit funds are savings institutions. They are of various types lacking any standardised form. A chit fund has regular members who make periodical subscriptions to the fund. The nidhis operate particularly in South India. They are in their characters as some kind of mutual benefit funds because their dealings are restricted only to the members.
<br>
            Indigenous bankers: Indigenous bankers are individuals or private firms which receive deposits and give loans and thereby operate as banks. Over the past four decades, the indigenous bankers have faced stiff competition from the commercial and cooperative banks. These bankers unwilling to separate their banking and non-banking activities.<br>
<br>
            <b>Moneylenders:</b> Moneylenders do not constitute one homogenous category. Broadly, they are of two types:
            <ul>

        <li>Professional moneylenders whose main activity is moneylending, and</li>

       <li> Non-professional moneylenders whose source of income is not moneylending.</li>
</ul>
The methods of operation of the moneylenders are not uniforms and their activities are generally localised.
<br>
<b>Organised Sector of the Indian Money Market:</b><br>

            The modern sector of the Indian money market is reasonably well-organised and integrated. Leaving aside some highly developed financial centers, the organised sector of the Indian money market is far more organised sector of the Indian money market is far more organised and developed than the money markets in most of the countries. The organised sector of the Indian money market comprises the RBI, commercial banks, foreign banks, cooperative banks, finance corporations, mutual funds and the Discount and Finance House of India (DFHI) Limited. Broadly the principal constituents of the organised sector of the Indian money market are:
<br>
            <b>The call money market:</b> The call money market exists in almost all developed money markets. It is generally the most sensitive part of the financial system. Any change in flow of funds and the demand for them is clearly reflected in it.
<br>
            <b>The treasury bill market:</b> The market which deals in treasury bills is known as the treasury bill market. In India, treasury bills are short-term liability of the central government. Some important treasury bills which have been used in the recent days are: 14-Day Intermediate Treasury Bill,14-Day Auction Treasury Bills, 91-Day Treasury Bills, 182-Day Treasury Bills, 364-Day Treasury Bills.
<br>
            <b> The repo market:</b> Repo is a money market instrument which helps in collateralised short-term borrowing and lending through sale/purchase operations in debt instruments. Repo is the rate at which banks borrow from the Reserve Bank, while reverse repo is used to drain excess cash lying with the banks.
<br>
            <b>The commercial bill market:</b> The commercial bill market is the sub-market in which the trade bills or the commercial bills are handled. The commercial bill is a bill drawn by one Merchant firm. On the other generally commercial bills arise out of domestic transactions. The legitimate purpose of a commercial bill is to reimburse the seller while the buyer delays payment.<br>

            <b>The certificate of deposit market:</b> A Certificate of Deposit (CD) is a   certificate issued by a bank to depositors of funds that remain on deposit at the bank for a specified period. Thus, CDS are tradable in the short-term money markets.RBI introduced CDs with the objective of widening the range of money market instruments and to provide investors greater flexibility in the deployment of their short-term surplus funds.
<br>
            <b>The commercial paper market:</b> The Commercial Paper (CP) is a short-term instrument of raising funds by corporate. It is essentially a sort of unsecured promissory note sold by the issuer to a banker or a security house. On the basis of the recommendation of Vaghul Committee, it was introduced in the Indian Money Market in January 1990. The CP can be issued by a listed Company which has a working Capital of not less than 5 crores.
            <br>

<b>Commercial Banking in India:</b>

            At the time of independence, Indian banking system was not sound. There were hundreds of private banks under unscrupulous management. Hence, in 1949 two major actions were taken very important from the point of view of structural reforms in the banking sector:

       <li> The Banking Regulation Act was passed. It gave extensive regulatory powers to RBI over the commercial banks.</li>

        <li>The nationalisation of the RBI.</li>

In the two decades following the enactment of the Banking Regulation Act, 1949, the Indian banking system developed in many respects. A significant change that occurred in this period was a steady decline in the importance of the non-scheduled commercial banks. The Banking Regulation Act provided extensive regulatory powers to the RBI and with that it became possible for it to carry out various structural reforms in the banking system.<br>

The establishment of the State Bank of India in 1955 and the creation of the State Bank group by nationalising eight regional banks in 1960 allowed scope for a new experiment in the Indian banking.
<br>
<b>‘Lead Bank’ Scheme and Branch Expansion</b><br>

            The area approach in respect of bank financing proposed by the Gadgil Study Group towards the end of 1969, culminated in the Lead Bank Scheme. It had the backing of Nariman Committee also.
<br>
            The Reserve Bank accepted the recommendations of the Narima Committee and prepared the ‘Lead Bank’ Scheme. Under the ‘Lead Bank’ Scheme districts were allotted to the State Bank Group, 14 nationalised banks and 3 private Indian banks.
<br>
            The Lead Bank was assigned a major development responsibility in the district allotted to it. It was expected to familiarise itself with socio-economic conditions prevailing in the district.<br>

<b>Indian Currency system</b><br>

            Indian monetary system is managed by Reserve Bank of India. It is based on inconvertible paper currency. The internal purposes are met by currency notes and coins. For external purposes it can be converted into foreign currencies.
<br>
<b>Important Terms Related to Currency</b><br>

            <b>Hard currency:</b> A currency traded in a foreign exchange market for which demand is persistently high relative to the supply, e.g. Pound and Euro Dollar.<br>

            <b>Soft currency:</b> A currency whose exchange rate is tending to fall because of persistent balance of payments deficits or because of the building up of speculative selling of currency in expectation of a change in its exchange rate. Governments are unwilling to hold a soft currency in their foreign exchange reserves.<br>

            <b>Reserve currency:</b> A currency which government and international institutions are willing to hold in their gold and foreign exchange reserves and finance as significant proportion of international trade.<br>

            <b>Fiat money:</b> Currency which is legally decreed as valid means of financing transactions. It is legal tender.<br>

            <b>Hot money:</b> Funds which flow into a country to take advantage of favourable rates of interest in that country. They influence the balance of payments and strengthen the exchange rate of the recipient country.<br>

<b>Classifications of Money</b>
<ul>
<b>Various Forms of Credit Money</b>
<ul>
        <li><b>Token coins:</b> All our coins are token coins in the sense that their value as money is far above the value of the metal contained in them.</li>

       <li><b> Representative token money:</b> This is usually of the form of paper, which is in effect a circulating ware house receipt for token coins or an equivalent amount of bullion that is banking it.</li>

       <li><b> Circulating promissory notes issued by central banks:</b> This is the greatest part of modern currency, and includes all currency notes in India issued by the Reserve Bank of India.</li>
</ul>
<b>Reserve Money:</b>

            In simple terms, Reserve Money can be called as government money produced by RBI (RBI is also taken as government) and let by the people and the banks. The reserve money is often called high powered money, basic money, primary money or monetary money. The ability of the banking system to create deposit money depends on the amount of reserve money available and the portion of which public holds in the form of currency. Reserve money is actually the cash held by the public and the banks.
<br>
<b>It is composed of</b>
<ul>
       <li> Currency in circulation-coins and currency notes held by the general public (C);</li>

        <li>Other deposits of the general public with RBI (OD);</li>

        <li>Cash Reserves of banks (CR) which are actually composed of two parts viz.</li>

&rarr;   Cash with banks themselves, and<br>

&rarr;   Bankers’ deposits with RBI (which banks correctly consider as their cash).<br>

<li>Thus, Reserve Money (RM) = C + OD + CR</li>
</ul>
<b>This means that RM is held party by the public (OD) and partly by banks (CR).</b><br><br>

CR is the very base for the entire demand deposit structure of the banking system. There are limits to the volume of credit creation in expansion of deposits; there should be enough cash with the ban king system to meet the depositors’ demand whenever it comes. The banking law requires a certain percentage of the deposits to be as cash reserve. The higher the percentage of cash reserve ratio + deposits required, the smaller the volume of credit creation and deposit expansion. The RBI can influence the volume of bank credit-bank deposits by manipulating the CR. If the whole of C + OD and CR is considered as reserve money, it is because C and OD also the potential of becoming CR without intervention from the RBI; the public could shift part of C into bank deposits or move part of OD into bank deposits.
<br>
<b>Monetary Aggregates</b><br>

            M0 = Currency in Circulation + Bankers Deposits with the RBI + ‘Other’ Deposits with the RBI.
<br><br>
            M1 = Currency with the Public + Demand Deposits with the Banking System + ‘Other’ Deposits with the RBI = Currency with the Public + Current Deposits with the Banking System + Demand Liabilities portion of Savings Deposits with the Banking System + ‘Other’ Deposits with the RBI.<br><br>

            M2 = M1 + Time Liabilities portion of Savings Deposits with the Banking System + Certificates of Deposit issued by Banks + Term Deposits (excluding FCNR (B) deposits) with a contractual maturity of up to and including one year with the Banking System = Currency with the Public + Current Deposits with the Banking System + Term Deposits (excluding FCNR (B) Deposits) with a contractual maturity up to and including one year with the Banking System; and
<br><br>
            M3 = M2 + Term Deposits (excluding FCNR (B) Deposits) with a contractual maturity of over one year with the Banking System + Call borrowings from ‘Non-Depository’ Financial Corporations by the Banking System.<br>
          </ul>
                      
