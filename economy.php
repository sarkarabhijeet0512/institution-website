<b>THE CONCEPT OF POVERTY</b><br><br>

       To understand the problem of poverty better, we must distinguish between absolute poverty and relative poverty.<br><br>

       Absolute Poverty of a person means that his income or consumption expenditure is so meagre that he lives below the minimum subsidence level. Because of his absolute poverty condition, he is not able to maintain his health and efficiency and in fact he may be starving.<br><br>

       On the other hand, Relative Poverty merely indicates the large inequalities of income. Those who are in the lower income brackets receive less than those in the higher income groups. The people with lower incomes are relatively poor compared to those with higher incomes, even though they may be living above the minimum level of subsistence. It is absolute poverty with which we are concerned when we talk of the problem of poverty in India. Affluent countries such as the USA have succeeded in removing absolute poverty from their people, but relative poverty, even prevails in them because of uneven distribution of income.<br><br>

       The Planning Commission adopted an alternative definition of poverty provided by the <b style="color: red;">“Task force on projections of minimum needs and effective consumption demand”</b>. Using the income poverty method, the Task Force has defined the poverty line as the midpoint of the monthly per capita expenditure class having a daily calorie intake of 2,400 per person in rural areas and 2,100 in urban areas.<br><br>

       <b>THE POVERTY REDUCTION STRATERGIES:</b>
       <br><br>
<ul>
        <li>Seek to attain the most rapid growth possible and sustain it over a decade so as to increase incomes and the demand for labour. The Tenth Plan target GDP growth rate is 8%.</li>

        <li>Ensure that the pattern of output is as labour-intensive and capital-savings as possible without distorting the normal comparative advantage of products. This will maximise the demand for labour and avoid rising unemployment.</li>

        <li>Ensure that the best attainable growth rates are achieved by the States and areas with the largest concentrations of the poor. This will be helped by implementation of the good governance actions, summarised above.</li>

        <li>Provide additional Central resources for States which undertake effective reform programmes.</li>

        <li>Continue to give top priority to primary education enrolment and attendance, especially of girls.</li>

        <li>Similarly, emphasise primary healthcare and adequate nutrition, especially of children and women.</li>

        <li>Implement area-based programmes where the problems of poverty are greatest or most intractable, and</li>

        <li>Target vulnerable minorities which might not be able to benefit from other programmes.</li>


</ul>

                      
