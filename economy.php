The MSS is an arrangement between the Government of India and the Reserve Bank to mop up the excess liquidity generated on account of the accretion to the foreign exchange assets of the Bank to neutrilize the monetary impact of capital flows.
<br>
            M1 measure represents the most liquid form of money among four money stock measures adopted by RBI. As we proceed from M1 to M4 the liquidity gets reduced. M3 is the most important component among all money stock measures which is generally termed as ‘Broad Money’.
<br>
<b>Marginal Standing Facility (MSF):</b>

            Marginal Standing Facility (MSF) is the rate at which scheduled banks could borrow funds overnight from RBI against approved government securities. Banks can borrow funds through MSF during acute cash shortage (considerable shortfall of liquidity). This measure has been introduced by RBI to regulate short-term asset liability mismatches more effectively. The MSF is pegged 100bps or 1% above the Repo Rate. To provide greater liquidity cushion the RBI introduced MSF. RBI announced the introduction ofMSF in May 2011.
                      
