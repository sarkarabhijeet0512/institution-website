      The Securities and Exchange Board of India (SEBI) set up in 1988 was given statutory recognition in 1992 on recommendations of the Narsimham Committe. Amongother things, the SEBI has been mandated to create an environment which would facilities mobilisation of adequate resources through the securities market and its efficient allocation. The  purpose and aims of SEBI are as follows:
<ul>
        <li>Tegulating the business in stock markets and other securities markets;</li>

