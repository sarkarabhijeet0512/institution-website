<b>Goals of Planning</b><br>

            Planning in India derives its objectives from the Directive Principles of State Policy enshrined in the constitution. Goals and objectives of planning can be summarised into two categories, i.e. short-term and long-term goals. Short-term goals of a plan refers to the immediate and most important targets which a particular plan endeavours to achieve. Such goals are called focus of plan as they reflect the strategy of a particular plan. On the other hand, long-term objectives of planning are the general guiding principles of the planning process and reflects the aspirations of a welfare state. The long-term objectives of planning in India have been more or less same in almost all the five-year plans and are as follows:
