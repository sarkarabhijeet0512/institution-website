            <li><b>Unbalanced growth strategy:</b> It was advocated by Streeten, Singer and Hirschman, It states that investment should be made in selected sectors rather than simultaneously in all sectors. It is based on the premise that underdeveloped countries posses limited resources which should be invested in high priority sectors.
