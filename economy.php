     Budget is a statement of estimated receipts and expenditure of the government for the ensuing fiscal year (i.e. 1st April to 31st march). It is also known as ‘Annual Financial Statement’. As per Article 112 of the constitution, the President shall cause to be laid a financial statement before both the Houses of Parliament at the commencement of every financial year of the estimated receipts and expenditure of the government for that year. Article 202 of the constitution provides that a similar financial statement for each states will be placed before the legislature of the respective state.
<br>
     <b>The annual budget statement presents four kinds of estimates:</b>
<ul>
       <li> Actual estimates of the preceding year</li>

        <li>Budget estimates of the current year</li>

        <li>Revised estimates of the current year</li>

       <li> Budget estimates for the preceding years</li>
</ul>
<b>Surplus, Balanced and Deficit Budget</b>

     <b> Surplus budget:</b> If the receipts of the government are more than its expenditure the government will be said to have a surplus budget.
