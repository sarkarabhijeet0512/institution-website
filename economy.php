<ul>
       <li>Structural unemployment is a longer-lasting for of unemployment caused by fundamental shifts in an economy and exacerbated by extraneous factors such as technology, competition and government policy. Reasons why structural unemployment occurs include workers lack of requisite job skills or that workers live too far from regions where jobs are available and cannot move closer. Jobs are available, but there is a serious mismatch between what companies need and what workers can offer.</li>

        <li>Cyclical unemployment is a factor of overall unemployment that relates to the cyclical trends in growth and production that occur within the business cycle. When business cycles are at their peak, cyclical unemployment will be low because total economic output is being maximised. When economic output falls, as measured by the Gross Domestic Product (GDP), the business cycle is low and cyclical unemployment will rise.</li>

        <li>Disguised unemployment exists where part of the labour force is either left without work or is working in a redundant manner where worker productivity is essentially zero. It is unemployment that does not affect aggregate output. An economy demonstrates disguised unemployment when productivity is low and too many workers are filling too few jobs.</li>

        <li>Seasonal unemployment occurs when there is limited need for a specific type of work to be performed during a certain time of the year. Professionals who work in a specific season often charge fees for their services, which provide the equivalent of an annual income, or work in multiple fields with different prime seasons. Industries where seasonal unemployment is common include farming, tourism and construction.</li>
</ul>
