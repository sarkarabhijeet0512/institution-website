<b>Control of credit by the RBI:</b>

            In India, the legal framework of the RBI’s control over the credit structure has been provided under the Reserve Bank of India Act, 1934 and the Banking Regulation act, 1949. The RBI has been empowered to use almost all the traditional instruments of credit control under the former; the latter has given it additional powers to use some other direct methods of credit regulation.
