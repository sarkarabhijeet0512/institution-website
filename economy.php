  <div class="menu-content taxation"><h3>TAXATION</h3><HR>

      A tax is a compulsory payment made by a person or a firm to the government without reference to any benefit, the payer may derive from the government.<br>

<b>Classification on the Basis of Method of Taxation</b>
<ul>
      <li><b> Progressive tax:</b> Under such taxation system, tax rate increases with the increase in income. It is based on the ‘ability to pay principle’ of taxation. Their real burden is more on rich and less on poor. It is an effective tool of fiscal policy to reduce inequalities of Income and wealth in a society.</li>

        <li><b>Proportional tax:</b> In the proportional tax system, tax rate remains constant irrespective of the level of income.</li>

        <li><b>Regressive tax:</b> In regressive taxation, tax rate decreases with increase in income. It is reverse of the progressive tax system. Such system of taxation is against the objective of a welfare state as it tends to accentuate income inequalities. So, it is only a theoretical concept, it has not been adopted by any country of the world.</li>

        <li><b>Degressive tax:</b> The degressive tax system is a mixture of proportional as well as progressive taxation system, so it is also called mild progressive tax. Under this system, tax rate increases with increase in income upto a limit than becomes constant.</li>

</ul> 

