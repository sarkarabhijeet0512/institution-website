            Agriculture sector needs well functioning markets to drive growth, employment and economic prosperity in rural areas of the country. In order to inject dynamism and efficiency into the marketing system, large investments are required for the development of post-harvest and cold chain infrastructure nearer to the farmer’s field. A major portion of this investment is expected from the private sector, for which an appropriate regulatory and policy environment is necessary. Alongside, enabling policies need to be put in place to encourage procurement of agricultural commodities directly from farmers’ field and to establish effective linkage between the farm production and the retail chain and food processing industries. Accordingly, amendment to the State APMC Act for deregulation of marketing system in the country is suggested to promote investment in marketing infrastructure, motivating corporate sector to undertake direct marketing and to facilitate a national integrated market.
