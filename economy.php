<b>Gilt-Edged Market:</b>

      The gilt-edged market is the market in government securities or the securities guaranteed (as to both principal and interest) by the government. Since the government cannot default on its payment obligations, the government securities are risk free and hence are known as gilt-edged (which means ‘of the best quality’).
<br>
<b>Difference Between Money and Capital Markets</b><br>
<ul>
        <li>The money market deals in short-term funds which are used for financing current business operations and short-term needs of the government. On the other hand, the capital market deals in long-term funds required by industry and government.</li>

        <li>Short-term funds in the money market refer to a period of less than a year, while in the capital market long-term funds refer to a period up to 25 years.</li>

        <li>The money market uses such instruments as promissory notes, bills of exchange, treasury bills, certificates of deposits, etc. On the other hand, the capital market used long-term securities such as shares, debentures and bonds of industrial concerns and of the government.</li>

        <li>The central bank, commercial banks, non-bank financial intermediaries and bill brokers deal in money market instruments. On the other hand, stock exchanges, mutual funds, leasing companies, investment banks, investment trusts, insurance companies, etc. deal in capital market instruments.</li>
</ul>
