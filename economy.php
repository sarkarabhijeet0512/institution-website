                    <div class="card-body" style="text-align: justify;"><h3>GOODS AND SERVICES TAX</h3><hr>

<b>101st Constitutional Amendment act & 122nd Constitutional Amendment Bill</b><br>

      GST is one indirect tax for the whole nation, which will make India one unified common market.

      GST is a single tax on the supply of goods and services, right from the manufacturer to the consumer.

      GST is a destination based tax means that tax would accrue to the State or the Union Territory where the consumption takes places. It follows a multi-stage collection mechanism.

      In this, tax is collected at every stage and the credit of tax paid at the previous stage is available as a set off at the next stage of transaction. This shifts the tax incidence near to the consumer and benefits the industry through better cash flows and better working capital management.
<br>
<b>GST Council</b><br>

      As per Article 279A (1) of the amended Constitution, the GST Council has to be Constituted by the President within 60 days of the commencement of Article 279A.<br>

       <b>GST Council shall consist of the following members namely:</b>
<ul>
        <li>Union Finance Minister-Chairperson</li>

        <li>The Union Minister of State, in-charge of Revenue of finance-Member</li>

        <li>The minister-in-charge of finance or taxation or any other Minister nominated by each State Government-Members</li>
</ul>

<b>Structure:</b>

      It would be a dual GST with the Centre and States simultaneously levying tax on a common tax base.
    <ul>
        <li>Central tax (CGST)-levied by the Centre on intra-State supply of goods and services.</li>

        <li>State tax (SGST)/Union Territory tax (UTGST)- levied by the States including Union Territories without legislature.</li>
      </ul>
      An Integrated tax (IGST) would be levied and collected by the Centre on inter-State supply of goods and services. Accounts would be settled periodically between the Centre and the States to ensure that the SGST/UTGST portion of IGST is transferred to the destination State where the goods or services are eventually consumed.
      <br>
      <br>

<b>GST would apply to all the goods except.</b>
      <ul>
        <li>Alcoholic liquor for human consumption.</li>

        <li>Five petroleum products, viz. petroleum crude, motor spirit (petrol), high speed diesel, natural gas and aviation turbine fuel.</li>
      </ul>
<b>It would apply to all services barring a few to be specified.</b>

      GST would replace the following taxes currently levied and collected by the Centre:
      <ul>
        <li>Central Excise Duty</li>

       <li> Duties of Excise (Medicinal and Toilet Preparations)</li>

        <li>Additional Duties of Excise (Goods of Special Importance)</li>

        <li>Additional Duties of Excise (Textiles and Textile Products)</li>

        <li>Additional Duties of Customs (commonly known as CVD)</li>

        <li>Special Additional Duty of Customs (SAD)</li>

        <li>Service TAX</li>

       <li>Central Surcharges and Cesses so far as they relate to supply of goods and services</li>
     </ul>
<b>State taxes that would be subsumed under the GST are:</b>
    <ul>
        <li>State VAT</li>

        <li>Central Sales Tax</li>

        <li>Luxury Tax</li>

        <li>Entry Tax (all forms)</li>

        <li>Entertainment and Amusement Tax (except when levied by the local bodies)</li>

        <li>Taxes on advertisements</li>

        <li>Purchase Tax</li>

        <li>axes on lotteries, betting and gambling</li>

        <li>State Surcharges and Cesses so far as they relate to supply of goods and services</li>
      </ul>
<b>GSTN:</b>

      A Special Purpose Vehicle called the GSTN has been set up to cater to the needs of GST. The GSTN shall provide a shared IT infrastructure and services to Central and State Governments, taxpayers and other stakeholders for implementation of GST.<br>

      <b>The functions of the GSTN would, include:</b>
        <ul>
        <li>Facilitating registration;</li>

        <li>Forwarding the returns to Central and State authorities;</li>

        <li>Computation and settlement of IGST;</li>

        <li>Matching of tax payment details with banking network;</li>

        <li>Providing various MIS reports to the Central and the State Governments based on the taxpayer return information;</li>

        <li>Providing analysis of taxpayers’ profile;</li>

        <li>Running the matching engine for matching, reversal and reclaim of input tax credit.</li>
      </ul>
<b>Minimum Alternative Tax:</b>

      The Minimum Alternative Tax (MAT) is the minimum amount of tax that a profitable company has to pay even if it is not liable to pay any tax as per the normal tax communication. There were instances when the earnings of some of the biggest Indian companies was more than the gross revenues of some states but these companies paid no taxes by taking advantage of various tax incentives. The government noting this anomaly; introduced MAT in 1983.<br>

      Very few countries apart from India have levied the Minimum Alternative. Tax USA is one. In USA, it is known as the Alternative Minimum Tax (AMT).
<br><br>
<b>Laffer Curve:</b>

      It depicts the relation between the tax rate and tax revenue. It shows that as the tax rate increases, tax revenue also increase, but after optimum level, tax revenue declines with increase in the tax rate.<br><br>

<b>Mechanism of Resource Transfer:</b>

      The basic principle of federal finance is that all the constituent units should have adequate sources of revenue. It has further been realised that the States, left to themselves, cannot raise sufficient revenues to ,meet their functional needs. Moreover, wide inter-State disparities exists. An important objective of development effort is to remove these regional disparities. A consideration of these different factors points to the need for a balancing factor. Such a balancing factor is provided by the principle of transfer. Our financial system has provided for the transfer of resources from the Centre to the States. The important means of resource transfer are as follows:
        <ul>
       <li> Proceeds of some taxes assigned to the State: As described, above proceeds of some taxes that are levied and collected by the Centre are transferred to the States.</li>

        <li><b>Tax-sharing:</b> Similarly, proceeds of taxes that are levied and collected by the Centre are shared with the States, e.g. income tax and Union excise duties.</li>

        <li><b>Grants-in-Aids:</b> Provisions exist in our financial system for grants-in-aid of revenue from the Centre to the State. Grants may be broadly classified as ‘plan’ and ‘non-plan’ grants. The ‘plan grants’ are made on the recommendations of the Planning Commission whereas the ‘non-plan grants’ are made on the recommendations of the Financial Commission.</li>

        <li><b>Loan:</b> The Central is authorised to give loans to the State Governments subject to such conditions as may be laid down by or under any law made by Parliament. The loans are charged to the Consolidated Fund of India.</li>

        <li><b>Other source of transfer:</b> In addition to transfer of resources from the Centre to the States according to the recommendations of the Finance Commission, there are two other sources of transfer:</li>

  &rarr; Assistance of plan purpose from the Planning Commission, and<br>

  &rarr; iscretionary grants from the Centre to the States.
</ul>
                      
