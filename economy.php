  <div class="menu-content monetary-policy"><h3>MONETARY POLICY TERMS</h3><hr>

<b>Bank rate:</b>

            This is the rate at which central bank (RBI) lends money to other banks or financial institutions. If the bank rate goes up, long-term interest rates also tend to move up, and vice-versa. Thus, it can said that in case bank rate is hiked, in all likelihood banks will hikes their own lending rates to ensure and they continue to make a profit.
            <br>
<b>Monetary policy objectives:</b>

            Going by convention, a central bank has two sets of tools-quantitative and qualitative-to signal easing or tight money conditions, depending on its policy objective. While quantitative tools would include imposing Cash reserve Requirements (CRR) for banks, fixing the repo or reverse-repo rates, the bank rate and prescribing the level of growth in the financial markets, pursuant with its growth objective for the economy. Popular qualitative measures would include imposing margins on certain loans and moral suasion. However, RBI often tweaks only the repo or reverse-repo rates and CRR. CRR is the proportion of deposits banks need to keep with the RBI.<br>

<b>Cash Reserve Ratio (CRR):</b>

            Banks are required to maintain a part or portion of the deposits they mobilise as cash with the central banks as CRR. Banks are required to park these funds on the average daily deposit mobilised on a fortnightly basis. This measure serves the policy objective instantly as liquidity is either infused or impounded almost immediately. However, this is not a very popular tool with central banks globally, though used by RBI often. Back home, even though SLR (the portion of deposits raised by banks that needs to be parked in government bonds, effectively controlling the amount of lendable funds in the system) is a tool that is in vogue. However, it is not often that RBI revises the SLR. RBI lends to banks at repo rate to manage their daily liquidity.<br>

<b>Repo rate:</b>

            It is essentially a short-term policy signal, the discount rate at which the central bank buys securities from commercial banks and infuses liquidity into the system. In other words, it is the rate at which the central bank lends to commercial banks to manage their daily liquidity. The intent is to signal the cost of funds in the system and indirectly influence the quantum of growth in funds while the bank rate is a signal for long-term rates and is a refinance rate for commercial banks. However, the bank rate has not been revised by RBI for over a decade now. RBI sells securities to banks at reverse-repo rate to suck out excess liquidity from the system.<br>

<b>Reverse-repo rate:</b>

Conversely, reverse-repo is the rate at which the central bank sells securities to banks in order to suck out excess liquidity from the system. If RBI wants to discourage banks from piling up idle cash, it reduces such rates. This will then force banks to look at other options to deploy their funds and earn better returns. Under its current liquidity management strategy, the repo and reverse-repo rates serve as a corridor for the movement of the overnight call money rates for banks. RBI initiated measures to keep the country at bay from the global financial crisis.<br>

<b>Statutory Liquidity Ratio:</b>

Every bank is required to maintain at the close of business every day, a minimum proportion of their Net Demand and Time Liabilities as liquid assets in the form of cash, gold and unencumbered approved securities. The ratio of liquid assets to demand and time liabilities is known as Statutory Liquidity Ratio (SLR). RBI is empowered to increase this ratio upto 40%. An increase in SLR also restricts the bank’s leverage position to pump more money into the economy.<br>
