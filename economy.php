                    <div class="card-body" style="text-align: justify;"><h2>FOREIGN EXCHANGE RATE SYSTEMS</h2><hr>

       Foreign exchange rate refers to the rate at which one unit of a currency of a country can be exchanged for the number of unit of another currency. It is the price of a currency in terms of another currency.<br>

       <b>Foreign exchange market:</b> The foreign exchange market is the market where the national currencies are traded for one another. There are two segments of the foreign exchange market, i.e., spot market and forward market.<br>

<b>Types of Exchange Rate Systems</b>
<br><ul>
        <li><b>Fixed exchange rate system:</b> Under system, exchange rate is officially declared by the monetary authority (Central Bank) and it is fixed. There are two variants of system:</li>

  &rarr;<b>Gold Standard System:</b> Under gold standard (or mint par value) system, the exchange rate of each currency is fixed in terms of gold.<br>

  &rarr; <b>Adjustable peg system:</b> Under adjustable peg (or Briton Woods) system, the exchange rate of each country was fixed in terms of US dollar, which was made convertible into gold at a fixed price. Adjustment in the parity value of a currency was allowed by the IMF under certain circumstances.

        <li><b>Wider bands:</b> Under wider bands system, wider adjustment in the fixed exchange rate is allowed. It allows adjustments upto 10% around the parity value between any two currencies.</li>

        <li><b>Crawling peg:</b> This system allows small but regular adjustment in the exchange rate bands for different currencies.</li>

        <li><b>Managed float:</b> Under this system, the exchange rate is market determined but the central bank possesses authority to intervene in the exchange rate determination by influencing the demand and supply of currencies. There are officially declared rules and guidelines for the intervention by the monetary authority. Managed floating with the absence of rules and regulations could be vulnerable to abuse of intervention. A particular country could manipulate the exchange rate of its currency to the detriment of other countries. This behaviour is called as ‘Dirty Float’.</li>

        <li><b>Flexible or floating exchange rate system:</b> Under this system, the exchange rate is determined by the demand and supply of the currencies concerned in the foreign exchange market. The exchange rate is freely determined by the market forces without any government intervention.</li>

 
</ul>
 

<b>India’s Exchange rate Policy</b>
<br>
      The Indian rupee was historically linked to the British pound sterling till 1946. Soon after Second World War, the IMF was set up and as the founder member of IMF, India had to fix and maintain the external value of the rupee in terms of gold or the (U.S. Dollar). The IMF system of fixed exchange rates collapsed in early 1970s. In 1975, the Government of India delinked the rupee from pound sterling and directed RBI to determine the rate of exchange of the rupee with respect to the exchange rate movements of the basket of currencies, viz. the Pound, U.S. Dollar, Yen and Deutsche Mark.
<br>
<b>Objectives of Exchange Rate Policy</b>
<ul>

       <b>Following are the important objective of exchange rate policy and management:</b>

       <li>To ensure that economic fundamentals are reflected in the external value of the rupee.</li>

        <li>To reduce excess volatility in exchange rates.</li>

        <li>To help maintain an adequate level of foreign exchange reserves.</li>

        <li>To prevent the emergence of destabilising speculative activities.</li>

        <li>To develop an orderly foreign exchange market.</li>

<b>Exchange Rate System in India</b><br>

      Presently, there is ‘managed float’ system of exchange rate in India. The exchange rate is broadly market determined with selective intervention by the RBI. The focus of the exchange rate management policy is to smoothen the fluctuation in the exchange rate with no specific rate target.
<br>
<b>Effective Exchange rate</b>
<br>
      The effective exchange rate measures the strength of the domestic currency in relation to the currencies of its major trading partners. According to Salvador, <b style="color: red;s">“Effective Exchange rate is a weighted average of the exchange rates between the currency of a country and its most important trading partners”</b>.<br>

      In India, the effective exchange rates are complied in relation of five and ten major trading partners. (i.e., 5-country index and 10-country index). The effective exchange rates of two types are as follows:

       <li><b>Nominal Effective Exchange rate (NEER):</b> It is the weighted average of exchange rates of a current vis-a-vis the currencies of major trading partners. It is not adjusted for the changes in the price level.</li>

        <li><b>Real Effective Exchange Rate (REFR):</b> It is that type of effective exchange rate which is adjusted for the relative changes in the price level across the concerned countries. It is based on constant prices or real exchange rates.</li>
</ul>
                      
