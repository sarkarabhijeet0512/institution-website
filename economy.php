<b>Organised Sector of the Indian Money Market:</b><br>

            The modern sector of the Indian money market is reasonably well-organised and integrated. Leaving aside some highly developed financial centers, the organised sector of the Indian money market is far more organised sector of the Indian money market is far more organised and developed than the money markets in most of the countries. The organised sector of the Indian money market comprises the RBI, commercial banks, foreign banks, cooperative banks, finance corporations, mutual funds and the Discount and Finance House of India (DFHI) Limited. Broadly the principal constituents of the organised sector of the Indian money market are:
<br>
            <b>The call money market:</b> The call money market exists in almost all developed money markets. It is generally the most sensitive part of the financial system. Any change in flow of funds and the demand for them is clearly reflected in it.
<br>
            <b>The treasury bill market:</b> The market which deals in treasury bills is known as the treasury bill market. In India, treasury bills are short-term liability of the central government. Some important treasury bills which have been used in the recent days are: 14-Day Intermediate Treasury Bill,14-Day Auction Treasury Bills, 91-Day Treasury Bills, 182-Day Treasury Bills, 364-Day Treasury Bills.
