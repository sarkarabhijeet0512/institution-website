                    <div class="card-body" style="text-align: justify;"><h3>FOREIGN INVESTMENT</h3><hr>

      Foreign investment is the equity capital which comes in two forms. Investments made in financial assets is called Foreign Portfolio Investment (FPI). The investment made in physical assets by foreign individuals and firms is called Foreign Direct Investment (FDI).
<ul>
       <li><b> Direct foreign investment:</b> Foreign capital can enter India in the form of direct investment. Sometimes companies of advanced countries start their subsidiary offices and affiliates in India. Alternately foreigners may subscribe to stocks and debentures of concerns in India. This is known as portfolio or entire investment.</li>

       <li><b> Foreign collaboration:</b> There are three types of foreign collaborations-joint participation between private parties, between foreign firms and Indian Government and between foreign governments and Indian Government.</li>

        <li><b>Inter-government loans:</b> Since 1946, the World Bank and its affiliates have been important suppliers of capital to India. International Monetary Fund (IMF), Aid India Consortium, Asian Development Bank (ADB) and the World Bank have been the major sources of external assistance to India recent years.</li>

        <li><b>External Commercial Borrowing ( ECB):</b> India has also been tapping export credit agencies like the US Exim Bank, the Japanese Exim Bank, ECGC of the UK, etc., to obtain a major portion of the commercial borrowing from the capital market.</li>
</ul>
<b>Foreign Portfolio Investment</b><br>

      It refers to the purchase of assets abroad but without any control over them. It involves investment in bonds, shares, securities or deposits with banks abroad. FPI is mainly a financial investment and can move out of a country at a short notice. The sources of portfolio investment are as follows:
<ul>
        <li><b>Foreign Institutional Investors (FIIs):</b> These are investment institutions like mutual funds, venture capital funds, merchant banks, insurance funds etc., which invest in foreign securities markets.</li>

        <li><b>Euro issues:</b> It refers to the issue of securities to raise funds from a foreign financial markets.</li>

        <li><b>Global Depository Receipts (GDRs):</b> GDRs are dollar denominated securities issued by a foreign financial intermediary called Overseas Depository Bank (ODB) in lieu of the underlying domestic securities. Such securities (GDRs) can be freely traded in the foreign capital markets.</li>

        <li><b>American Depository receipts (ADRs):</b> The Global Depository receipts (GDRs) issued in USA are called ADRs.</li>

        <li><b>Participatory Notes (PN):</b> These are offshore derivative instruments issued by a Foreign Institutional Investor (FII) against underlying domestic securities.</li>

      After a subdued performance in 2002-03, portfolio flows rebound strongly in US$ 11.4 billion in 2003-04. The impressive performance of portfolio investment in 2003-04 was on account of a rapid influx of FII inflows, driven by the heavy bullish sentiments prevailing in the Indian stock markets.<br>
</ul>
<b>Foreign Direct Investment</b><br>

      Foreign Direct Investment (FDI) refers to the purchase of assets abroad along with the power to control them. It is the most preferred form of capital inflows as it is non-debt and long-term in nature. FDI can be made by a Multinational Corporation (MNC) or by its subsidiary or by way of joint enterprise involving a MNC and a domestic partner.

      The benefits from FDI do not accrue automatically and evenly across countries and sectors. In order to reap the maximum benefits from FDI, there is a need to establish a transparent, broad and effective enabling policy environment for investment and to put in place appropriate framework for their implementation. Such an environment must provide incentives for innovations and improvement of skills and contribute towards improved competitiveness.
                      
