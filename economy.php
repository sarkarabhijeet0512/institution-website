<ul>
       <li><b> Ad-valorem tax:</b> Under this tax system, tax is imposed on the basis of the value of the commodity. Such taxes are equitable in incidents as they conform with the ‘ability to pay principle’ of taxation.</li>

        <li><b>Specific tax:</b> Such taxes are imposed on the basis of specific attributes or qualities of commodities like unit, weight, length, size or volume, e.g. specific excise duty may be levied on the cloth in the length units.</li>
</ul>
