</ul>
      SEBI has been vested with wide-ranging powers. Firstly, to oversee the constitution as well as the operations of mutual funds including presentation of accounts, following the decision to allow the entry of private sector and joint sector mutual funds. Secondly, all stock exchanges in the country have been brought under the annual inspection regime of SEBI for ensuring orderly growth of stock markets and investors protection. Thirdly, with the repealing of the Capital Issue (Control) Act, 1947, in May 1992, SEBI has been made the regulatory authority in regard to new issues of companies. An amendment to the SEBI Act (1992) carried out of March 25, 1995 has empowered SEBI to register and regulate new intermediaries associated with the securities market are now regulated by SEBI.
