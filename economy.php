        <li>Prohibiting fraudulent and unfair trade practices relating to securities market;</li>

        <li>Promoting investors’ education and training of intermediaries of securities market;</li>

        <li>Prohibiting insider trading in securities;</li>

        <li>Regulating substantial acquisition of shares and takeover of companies; and</li>

