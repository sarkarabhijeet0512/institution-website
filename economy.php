<b>Participatory Notes (P-Notes):</b>

      Participatory notes, also referred to as “P-notes,” are financial instruments used by investors or hedge funds that are not registered with the Securities and Exchange Board of India (SEBI) to invest in India securities. Any dividends or capital gains collected from the underlying securities go back to the investors. Indian regulators are against participatory notes because they fear that hedge funds acting through participatory notes will cause economic volatility in India’s exchanges.
                      
