  <div class="menu-content national-income"><h3>NATIONAL INCOME</h3><hr>
    <h6>Introduction</h6>

            National income is defined as the value of all final goods and services produced by the residents of a country, whether operating within the domestic territory of the country, or outside, in a year. National income is thus, a momentary expression of the current achievements of they people of expression of the current achievements of the people of a country expressed through their production activities.

            <b>National product is stock and quantitative concept.</b><br>

                        <b>National Product=Price * Quantity</b><br>

                                                   <b>NP= PQ</b><br>

            National income is the sum of return of all the resources involved in any economic activities. It is qualitative and dynamic concept and related to flow.

            <b>National income = Wage + Rent + Interest + Profit</b><br>

            National income can be defined in many ways. In simplest terms, it can be described as the income of all the people of a country during a year.<br>

            It is also defined as the money value of all the final goods and services product in an economy over a specified period of time. It indicates the performance of an economy in terms of production of goods and services. It is used as a measure of economic growth and reflects the productive power of an economy to turn out goods and services for the satisfaction of human wants.<br>

            <b>Gross Domestic Product (GDP):</b> GDP is monetary value of final goods and services which are produced by general residents of country in own physical boundary.<br>

            <b>GDP = C + I + G</b><br>

            <b>Here, c = Consumption, I = Investment</b><br>

            <b>G = Government Expenditure</b><br>

            <b>Net Domestic Product (NDP): It is the difference between GDP and depreciation.</b><br>

            <b>NDP = GDP – Depreciation, NDP = C + I + G – D</b><br>

            <b>Depreciation:</b> It refers to the reduction in the value of capital assets, (e.g. machinery building, equipment, etc). Due to normal wear and tear and technological obsolescence. Depreciation is also called consumption of fixed capital and capital consumption. Net value is obtained by reducing the values of depreciation from the value.<br>

            <b>Net value = Gross value – Depreciation</b>

            <b>Gross National Product (GNP):</b> GNP is the sum of values of all final goods and services which is produced by citizen of a country in financial year and net earned income from abroad.<br>

            <b>GNP = C + I + G + (X-M)</b><br>

            <b>X – Income earned from export, M – Income expenses on import</b><br>

            <b>Basic difference in GDP and GNP:</b> In case of GDP of net income earned from abroad excluded but in case of GNP it is included. In India only two years 1972-73 and 1976-77 GNP was more than GDP. These two years are to be called golden years of Indian economy.<br>

            <b>Net National Product (NNP):</b> It is difference between GNP and Depreciation.<br>

<b>NNP = GNP – Depreciation</b>
<br><br>
<b>Real income:</b> Real income is national income expressed in terms of general level of prices of a particular year taken as base. National income is the value of goods and services produced as expressed in terms of money at current prices. But it does not indicate the real state of the economy. It is possible that the net national product of goods and services this year might have been less than that of the last year, but owing to an increase in prices, the NNP might be higher this year. On the contrary, it is also possible that NNP might have increased but the price level might have fallen, as a result of which national income would appear to be less than that of the last year. In both the situations, the national income does not depict the real state of the country. To rectify such as a mistake, the concept of real income has been evolved.<br>

            Factor Cost versus Market Price: Factor cost (FC) refers to the payments made to the factors of production (i.e. labour, land, capital, and entrepreneurship) of goods and services, while Market Price (MP) refers to the market value of the goods and services produced. Market price of the goods and services is arrived at by adding Net Indirect Taxes (i.e. Indirect Taxes – Subsidies) in the cost of production (FC).<br>

            <b>MP = FC +== Net Indirect Taxes</b><br>

            <b>e.g. GDP (MP) = GDP (FC) + Indirect taxes – Subsidies</b><br>

            <b>or, GNP MP = GNP FC + Indirect Taxes – Subsidies</b><br>

            Current Price versus Constant Price: National income at current prices shows the value of the goods and services at the market prices prevailing in the current year (i.e. the same year in which they were produced), while national income at constant prices shows the value of the goods and services at the base year (1993-94) prices. National income at current prices is called ‘Nominal national Income’ while national income at constant prices is called Real National Income, as it offers the effects of inflation.<br>

            <b>Deflation of GDP:</b> Conversion of Nominal GDP into Real GDP by adjusting it for price level changes is called deflation of GDP<br>

<b>Real GDP Constant Price = GDP Current prices * 100/Price Index Number</b><br>

            For example, the value of nominal GDP was 28.38 lakh, crore in the financial year 2004-05 and the (mid-year) value of Wholesale Price Index (WPI) was 185.57, then the value of Real GDP can be calculated as follows:<br><br>

<b>Real GDP = 28.38 lakh crore * 100 /185.57 = 15.29 lakh crore</b><br><br>

            <b>Capital Output Ratio:</b> The required capital for production of one unit is called capital output ratio.<br><br>

            <b>Capital Output Rato = Total capital / Total production</b><br><br>

           <b> C.O.R = Total capital/ Total Production</b><br><br>

            <b>Average Capital Output Ratio (A.C.O.R)</b><br><br>

            <b>Incremental Capital Output Ratio (I.C.O.R)</b><br><br>

            I.C.O.R = Increase in capital / Increase in production
            <br>
            <br>

<b>Methods of Calculating National Income</b>
<ul>
    <li>        Three methods are usually adopted to calculate national income.</li>

       <li><b> The Income Method:</b> This method measures national income at the stage of factor payments made to primary factor for the use of their factor services.
<br>
National Income = Compensation of employees + capital incomes on operating surplus + mixed incomes + net factor income from abroad.</li>

        <li><b>The Product Method or the Inventory Method:</b> It takes into consideration the total goods and services consumed in a year at market prices. This method needs an accurate census of production as well as wages census.</li>

        <li><b>Expenditure Method:</b> From the expenditure viewpoint, national income is the sum total of expenditure incurred on goods and services during one year in a country. It includes private consumption expenditure (C), domestic private invest (I), net foreign investment (X-M) and government expenditure (G).
<br>
National Income = C + I + G + (X-M) = GNP</li>


 </ul>


<b>National Income Aggregates</b>
<ul>

       <li><b> Gross Domestic Product at Market Prices (GDPmp):</b>GDP at market prices is the value of all fixed goods and services at prices prevailing in the market produced in the domestic territory of a country during a given year. Being gross, it is inclusive of depreciation, i.e. consumption of fixed capital in the process of production has not been excluded.</li>

       <li><b> Gross National Product at Market Prices (GNPmp):</b> It is defined as the aggregate market value of all final goods and services produced by national of a country during a year. It differs from GDPMP to the extent of net factor incomes earned from abroad. To get GNPmp from GDPmp, we have to add to GDPmp the income earned from abroad by the residents of country in the forms of wages, interests and profits.</li>

<b>GNPmp = GDPmp + Net Factor income from abroad</b>

       <li><b> Net Domestic Product at Market Prices (NDPmp):</b> Net domestic product at market prices is the value of all final goods and services at prices prevailing in the market produced in the domestic territory of a country during a given year after making allowance from depreciation.</li>

<b>Net Domestic Product at market prices is the difference between gross domestic product at market prices and depreciation.</b>
<br>
NDPmp = GDPmp - Depreciation
<br>
NDPmp is the measure of national income which is available for consumption and investment in the economy.
<br>
       <li><b>Net National Product at Market Prices (NNPmp):</b> Net National Product at market prices is similar to NDPmp. It shows the market value of all final goods and services produced by the residents of a country during a year after making allowance for depreciation. It differs from GNPmp in that the depreciation of capital is subtracted.
<br>
NNPmp = GNPmp – Depreciation
<br>
It is considered to be more accurate measure of  the true output of the economy than GNPmp.</li>

       <li> <b>Gross Domestic Product at Factor Cost (GDPfc):</b> It is the sum total of earnings received by various factors of production in terms of wages, interest, rent, profits etc. Within the domestic territory of a country in a year. If we deduct net indirect taxes from Gross Domestic Product at market prices, we get gross domestic product at factor cost. Being gross, it makes no provision for the depreciation.
<br>
GDPfc = GDPmp – Net Indirect Taxes</li>

       <li> <b>Gross National Product at Factor Cost (GNPfc):</b> It is the sum total of earnings received by various factors of production in terms of wages, rent, interest etc. By normal residents of a country in a year. It differs from GNPmp to the extent of net indirect taxes.
<br>
GNPfc = GNPmp – Net Indirect taxes</li>

       <li> <b>Net Domestic Product at Factor Cost (NDPfc):</b> Net Domestic Product at factor cost is the estimate of the domestic product in terms of earnings of factors of production within the domestic territory of a country in a year. It differs from gross domestic product at factor cost to the extent of depreciation.
<br>
NDPfc = GDPfc – Depreciation</li>

       <li> <b>Net National Product at Factor Cost (NNPfc) or National Income:</b> National income can be defined as factor incomes accruing to the residents of a country. It is the sum total of domestic factor income and net factor incomes earned from abroad.
<br>
NNPfc (or National Income) = domestic Factor Incomes + Net Factor Income earned From Abroad NNPfc = GNPfc – Depreciation
<br>
National income can also be found out by deducting depreciation from gross product at factor cost.
<br>
New Series Estimates of National income, Consumption Expenditure, Saving and Capital Formation (Base Year 2011-12)</li>

       <li> The Ministry of Statistics & Programme Implementation has released the new series of national accounts, revising the base year from 2004-05 to 2011-12 base year of national accounts was last revised in January 2010.</li>

       <li> Base year revisions differ from annual revisions in National Account primarily because of nature of changes. In annual revisions, changes are made only on the basis of updated data becoming available without making any changes in the conceptual framework or using any new data source, to ensure strict comparison over years. In case of base years revisions, apart from a shift in the reference year for measuring the real growth, conceptual changes, as recommended by the international guidelines are incorporated. Further, statistical changes like revisions, in the methodology of compilation, adoption of latest classification systems and inclusion of new and recent data sources are also made. Changes are also made in the presentation of estimates to improve ease of understanding for analysis and facilitate international comparability.</li>

        <li> Improvements as noted above, especially incorporation of new datasets, have resulted in a correction in the level of GDP, which is likely to affect a wide range of indicators where it is used as a reference point: for instance, trends in public expenditure, taxes and public sector debt that are conventionally analysed in terms of their ratios to nominal GDP. It may be noted that the level of revision in the present base revision is not large enough to affect any of these ratios significantly.
</li>
</ul>
