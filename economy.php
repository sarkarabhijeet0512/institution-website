<b>Liquidity Adjustment Facility (LAF):</b>

The Liquidity Adjustment facility (LAF) was introduced by the Reserve Bank in June 2000. It allows the Reserve Bank to manage market liquidity on a daily basis and also transmit interest rate signals to the market. The LAF, initially recommended by the Committee of Banking Sector Reforms (under the chairmanship of M. Narasimham), was introduced in stages in consonance with the level of market development and technological advances in payment and settlement systems. The LAF operates through reverse repo auctions, i.e., the sale of government securities from the RBI portfolio for absorption of liquidity and repo auctions i.e., buying of government securities for injection of liquidity on a daily basis, thereby creating a corridor for the call money rates and other short-term interest rates. The LAF has emerged as the principal operating instrument of monetary policy enabling the Reserve Bank to modulates short-term liquidity under varied financial market conditions.
