      Budget implies that government is misleading out more money from the economic system. It has a contractionary effect.
      <ol type="a">
                                         <li>Fall in the level of economic activity</li>

                                                    <li>Fall in investment</li>

                                                    <li>Fall in employment</li>

                                                          <li>Fall in income</li>

                                           <li>Fall in consumption and savings</li>
                                         </ol>

     <b> Balanced budget:</b> A balanced budget is one where the estimated revenue equals the estimated expenditure. If the receipts of the government equal its expenditure the government will said to have balanced budget. A balanced budget will have a neutral effect on the level of economic activity.<br>

      <b>Deficit budget:</b> It occurs when the overall expenditure of the government exceeds the overall receipts. Budgetary deficits is financed by printing of new currency notes by the government. If the receipts of the government are less than its expenditure the government will said to have a deficit budget.<br>

      <b>A deficit budget implies that the government is pumping in more money in the economic system than is pumping out. The process works as follows:</b><br>
          <ol type="a">
                                             <li>Expansion in economic activity</li>


                                             <li> Rise in investment</li>

                                                       <li>Rise in employment</li>

                                                           <li>Rise in income</li>

                                             <li>Rise in consumption and savings</li>
                                           </ol>

      This will move the economy further on the growth path. The Government in a developing economy always plans for a deficit budget.<br>

      <b>The budget shows the receipts and expenditure of the government (centre and states) under three heads:</b><br>
      <ul>

        <li><b>Consolidated fund:</b> It is the main account of the government and it consists of all the receipts of the government from taxes, loans and other receipts. No amount can be spent without prior sanction of the Parliament (or state legislature).</li>

        <li><b>Contingency fund:</b> It consists of the sum placed at the disposal of the President to meet unforeseen expenditure. The prior sanction of the Parliament or state legislature is not required under this fund but it is sought to replenish the fund.</li>

        <li><b>Public account:</b> It consists of all receipts and payments which are in the nature of a deposit account with the government such as small savings, provident funds, etc. No legislative sanction is required for withdrawal of money from this fund as it does not belong to the government and comes under the public account.</li>
</ul>
      <b>Union Finance Minister’s speech:</b> Finance Minister’s speech gives a broad overview of the Budget proposals. Generally it is in two parts. The first deals with a review of implementation of the preceding year’s schemes, revised estimates for the completed year and the budget estimates for the next year, without taking into account the impact of budget proposals. Part two of the speech deals with revenue mobilisation through tax proposals.<br><br>

      <b>Annual Financial statement:</b> Annual Financial Statement (AFS) is the main Budget document. Under Article 112 of the Constitution, a statement of estimated receipts and expenditure of the Government of India has to be laid before the Parliament in respect of every financial year from 1st April to 31st March. It shows the receipts and payments of government under three accounts-Consolidated Fund, Contingency Fund and the Public Account.<br><br>

      <b>Demands for grants:</b> The estimates of expenditure from the Consolidated Fund, included in the Annual Financial Statement and required to be voted by the Lok Sabha, are submitted in the form of Demands for Grants in pursuance of Article 113 of the Constitution. Generally, one demand for Grant is presented in respect of each ministry or department.<br><br>

      <b>Budget at a glance:</b> The Budget at a glance gives an overview of the budgetary proposals. It gives a breakup of tax and other receipts as well as expenditures (plan and non-plan), allocations of outlays by ministries and resource transfers to states and UTs, the projections on revenue deficit and primary deficit, etc.<br><br>

      <b>Finance Bill:</b> The proposals of government for levy of new taxes, modification of the existing tax structure or continuance of the existing tax structure are submitted to the Parliament through the Finance Bill. To facilitate easy comprehension of the budget, certain explanatory documents are presented along with the budget.<br><br>

       <b>Appropriation Bill:</b> After the Lok Sabha on the Demands for Grants, Parliament’s approval to the withdrawal from the Consolidated fund of the amounts so voted and of the amount required to meet the expenditure ‘changed’ on the Consolidated Fund is sought through the Appropriation Bill.<br>

      The presentation of Budget is followed by a general discussion on it in both the Houses of parliament: Estimates of expenditure from the Consolidated Fund of India are placed before the Lok Sabha in the form of ‘Demand of Grants’. All withdrawals of money from the Consolidated Fund are therefore authorised by an Appropriation Act passed by the Parliament every year. Tax Proposals of the Budget are embodied in Bill which is passed as the ‘Finance Act’ of the year. Estimation of receipts and expenditure are similarly presented by the state governments in their legislature before the beginning of the financial year.<br>

<h6>Structure of the Budget</h6>
<ul>
  
      <li><b>Structure of the Budget refers to the components of the budget. It has two broad components:</b></li><br>

&rarr; Budget receipts, and<br>

&rarr;  Budget expenditure.<br>

        <li><b>Budget receipts refer to estimated money receipts of the government from all sources during the fiscal year. Broadly, the budget receipts are classified as:</b></li>

&rarr;  <b>Revenue receipts:</b> Such receipts accrue to the government on account of its current activities and are generally recurrent in nature. For example, tax receipts like income tax, excise tax etc. and non-tax revenue receipts like income from PSUs, interest received etc.<br>

&rarr;   <b>Capital receipts:</b> Such receipts either create liabilities (like borrowings) or reduce assets like disinvestment, recovery of loans, etc.<br>

        <li><b>Budget expenditure refers to the estimated expenditure of the government on its developmental and non-developmental programmes (or on its plan and non-plan programmes) during the fiscal year. Budget expenditure is also classified into two broad categories:</b></li>

&rarr;   Revenue expenditure: Such expenditure is incurred on day-to-day activities of the government and neither create assets nor reduce liabilities, e.g. interest payments, subsidies, etc.<br>

&rarr;  Capital expenditure: Such expenditure either creates asset or reduces liability of the government, e.g. expenditure on construction of road, bridge, building etc.<br>
</ul>
                      
