<b>Export Finance</b><br>

      Pre-shipment and post-shipment credits are the customary banking credit facilities for exports. Traditional exports (such as tea), with foreign exchange from such exports realised typically within 180 days from shipment, require finance which is short-term in nature. Non-traditional exports, covering products such as capital goods, services and project exports, however, require deferred credit over the medium (1-5 years) and long-term (beyond 5 years and up to 10 years). Commercial banks, with their liability structure short-term in character, concentrate on traditional, short-term export finance. Export credit of schedule commercial banks amounted to 65,914 crore at end-March 2005. Export-Import Bank of India (Exim Bank), with its medium/long-term liability structure, concentrates primarily on medium to long-term export finance, besides working capital. Exim Bank also provides refinance and rediscounting facilities to commercial banks so that short-term exports are not affected by lack of finance. Exim Bank’s lending has steadily increased over the years. In 2005-06, Exim Bank’s approved loans increased by over 29% to 20,489 crore.<br>

      Exim Bank’s provides finance at various stages of the export cycle-import finance, product, development, production, marketing, pre-shipment and post-shipment-as well as for overseas investment. It has a range of export credit programmes, such as supplier’s and buyer’s credit, for Indian companies executing contracts overseas for projects, products, as well as services. The Exim Bank also extends guarantee facilities to facilitate Indian companies in executing export contracts and extends lines of credit, on its owner at the behest of the Government of India, to overseas government, or to agencies nominated by them, or to financial institutions overseas for encouraging imports from India on deferred payment term. Exim Bank’s import lines of credit provide financing for imports of capital goods and related services particularly for enhancing export production capabilities of small and medium-sised export-oriented units.<br>

<b>Types of Foreign Trade Policy</b><br>

      <b>Free trade versus Protection:</b> Free trade policy refers to the trade policy that is free from all artificial barriers of trade like tariffs, quantitative restrictions and other devices obstructing the movement of goods between countries. Protection the other hand, refers to government policy of providing protection to the domestic industries from foreign competition. The domestic industries are protected by imposing import duties which raise the price of foreign goods and by imposing various trade barriers.<br>

     Outward oriented strategy versus Inward oriented strategy: Under outward-oriented strategy, trade and industrial policies do not discriminate between production for the domestic goods and foreign goods. Government imposes least restrictions on trade and foreign investment. While under the inward-oriented strategy, government accords high levels of protection for domestic industries through trade barriers and direct controls on imports and foreign investment.<br>

<b>Trade barriers</b><br>
<ul>
      Trade barriers refer to the government measures which obstructs the free flow of goods and services across national borders. Trade barriers can be classified under two groups i.e., tariff barriers and non-tariff barriers.<br>

        <li><b>Tariff barriers:</b>Tariff refer to the taxes imposed on internationally traded commodities when they cross national i.e., customs duties like import and export taxes.</li>
</ul>
<b>Non-tariff barriers (NTBs):</b> NTBs refers to the restrictions on trade other than custom duties. NTBs include Quantitative Restrictions (QRs), Import Licencing, cannalisation, foreign exchange controls, etc. QRs refer to the fixation of ceiling on the quantity or value of a commodity which can be imported or exported during a specific period.  

                      
