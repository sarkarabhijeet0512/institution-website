<b>Methods of Calculating National Income</b>
<ul>
    <li>        Three methods are usually adopted to calculate national income.</li>

       <li><b> The Income Method:</b> This method measures national income at the stage of factor payments made to primary factor for the use of their factor services.
<br>
National Income = Compensation of employees + capital incomes on operating surplus + mixed incomes + net factor income from abroad.</li>

        <li><b>The Product Method or the Inventory Method:</b> It takes into consideration the total goods and services consumed in a year at market prices. This method needs an accurate census of production as well as wages census.</li>

        <li><b>Expenditure Method:</b> From the expenditure viewpoint, national income is the sum total of expenditure incurred on goods and services during one year in a country. It includes private consumption expenditure (C), domestic private invest (I), net foreign investment (X-M) and government expenditure (G).
<br>
National Income = C + I + G + (X-M) = GNP</li>


 </ul>


<b>National Income Aggregates</b>
<ul>

       <li><b> Gross Domestic Product at Market Prices (GDPmp):</b>GDP at market prices is the value of all fixed goods and services at prices prevailing in the market produced in the domestic territory of a country during a given year. Being gross, it is inclusive of depreciation, i.e. consumption of fixed capital in the process of production has not been excluded.</li>

       <li><b> Gross National Product at Market Prices (GNPmp):</b> It is defined as the aggregate market value of all final goods and services produced by national of a country during a year. It differs from GDPMP to the extent of net factor incomes earned from abroad. To get GNPmp from GDPmp, we have to add to GDPmp the income earned from abroad by the residents of country in the forms of wages, interests and profits.</li>

<b>GNPmp = GDPmp + Net Factor income from abroad</b>

       <li><b> Net Domestic Product at Market Prices (NDPmp):</b> Net domestic product at market prices is the value of all final goods and services at prices prevailing in the market produced in the domestic territory of a country during a given year after making allowance from depreciation.</li>

<b>Net Domestic Product at market prices is the difference between gross domestic product at market prices and depreciation.</b>
<br>
NDPmp = GDPmp - Depreciation
<br>
NDPmp is the measure of national income which is available for consumption and investment in the economy.
<br>
       <li><b>Net National Product at Market Prices (NNPmp):</b> Net National Product at market prices is similar to NDPmp. It shows the market value of all final goods and services produced by the residents of a country during a year after making allowance for depreciation. It differs from GNPmp in that the depreciation of capital is subtracted.
<br>
NNPmp = GNPmp – Depreciation
<br>
It is considered to be more accurate measure of  the true output of the economy than GNPmp.</li>

       <li> <b>Gross Domestic Product at Factor Cost (GDPfc):</b> It is the sum total of earnings received by various factors of production in terms of wages, interest, rent, profits etc. Within the domestic territory of a country in a year. If we deduct net indirect taxes from Gross Domestic Product at market prices, we get gross domestic product at factor cost. Being gross, it makes no provision for the depreciation.
<br>
GDPfc = GDPmp – Net Indirect Taxes</li>

       <li> <b>Gross National Product at Factor Cost (GNPfc):</b> It is the sum total of earnings received by various factors of production in terms of wages, rent, interest etc. By normal residents of a country in a year. It differs from GNPmp to the extent of net indirect taxes.
<br>
GNPfc = GNPmp – Net Indirect taxes</li>

       <li> <b>Net Domestic Product at Factor Cost (NDPfc):</b> Net Domestic Product at factor cost is the estimate of the domestic product in terms of earnings of factors of production within the domestic territory of a country in a year. It differs from gross domestic product at factor cost to the extent of depreciation.
<br>
NDPfc = GDPfc – Depreciation</li>

       <li> <b>Net National Product at Factor Cost (NNPfc) or National Income:</b> National income can be defined as factor incomes accruing to the residents of a country. It is the sum total of domestic factor income and net factor incomes earned from abroad.
