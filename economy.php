<b>Indian Money Market:</b>

            Money Market refers to a mechanism whereby on the one hand borrowers manage to obtain short-term loanable funds and on the other; lenders succeed in getting credit worthy borrowers for their money. In any money market, commercial banks are the most important lenders. These banks are, however, not merely the lenders of money, they also create credit. The central banks role is important as the controller of credit. The Indian money market is not an integrated unit. It is broadly divided into two parts, viz. the unorganised and the organised.
<br><br>
<b>Unorganised Sector of the Indian Money Market</b><br>

            Unregulated non-bank financial intermediaries such as fanince companies, chit funds, nidhis, etc. indigenous banlers and moneylenders operate in the unorganised sector of the Indian money market. Lendings of the  moneylender are rather small, but some finance companies, chit funds and indigenous bankers do a considerable amount of business. They pursue banking business on traditional lines. Some of them are:
<br>
            Unregulated non-bank financial intermediaries: Loan companies (also called finance companies) are found in all parts of the country. The exact members of Loan Companies are not known. In addition, a large number of partnership firms and individuals are engaged in lending business. The chit funds are savings institutions. They are of various types lacking any standardised form. A chit fund has regular members who make periodical subscriptions to the fund. The nidhis operate particularly in South India. They are in their characters as some kind of mutual benefit funds because their dealings are restricted only to the members.
<br>
            Indigenous bankers: Indigenous bankers are individuals or private firms which receive deposits and give loans and thereby operate as banks. Over the past four decades, the indigenous bankers have faced stiff competition from the commercial and cooperative banks. These bankers unwilling to separate their banking and non-banking activities.<br>
<br>
            <b>Moneylenders:</b> Moneylenders do not constitute one homogenous category. Broadly, they are of two types:
            <ul>

        <li>Professional moneylenders whose main activity is moneylending, and</li>

       <li> Non-professional moneylenders whose source of income is not moneylending.</li>
</ul>
The methods of operation of the moneylenders are not uniforms and their activities are generally localised.
