            Indian monetary system is managed by Reserve Bank of India. It is based on inconvertible paper currency. The internal purposes are met by currency notes and coins. For external purposes it can be converted into foreign currencies.
<br>
<b>Important Terms Related to Currency</b><br>

            <b>Hard currency:</b> A currency traded in a foreign exchange market for which demand is persistently high relative to the supply, e.g. Pound and Euro Dollar.<br>

            <b>Soft currency:</b> A currency whose exchange rate is tending to fall because of persistent balance of payments deficits or because of the building up of speculative selling of currency in expectation of a change in its exchange rate. Governments are unwilling to hold a soft currency in their foreign exchange reserves.<br>

