  <div class="menu-content Committes"><h1>COMMITTEES OF THE PARLIAMENT</h1><hr>
<p>The work done by the Parliament in modern times is not only varied in nature, but considerable in volume. The time at its disposal is limited. It cannot therefore give close consideration to all the legislative and other matters that come up before it. A good deal of its business is therefore transacted by what are called the Parliamentary Committees. Parliamentary Committees are of two kinds:</p>
<ul>
    <li>Ad hoc Committees, and</li>
    <li>The Standing Committees</li>
</ul>
<p>Ad hoc Committee is appointed for a specific purpose and they cease to exist when they finish the tasks assigned to them and submit a report. The principal Ad hoc Committees are the Select and Joint Committees on Bills. Others like the Railway Convention Committee, the Committees on the Draft Five Year Plans and the Hindi Equivalents Committees were appointed for specific purposes. Apart from the Ad hoc Committees, each House of Parliament has Standing Committees like the Business Advisory Committee, the Committee on Petitions, the Committee of Privileges and the Rules Committee, etc.</p>
<p><strong>Composition and Functions of Select and Joint Committees</strong></p>
<p>When a Bill comes up before a House for general discussion, it is open to that House to refer it to a Select Committee of the House or a Joint Committee of the two Houses. A motion has to be moved and adopted to this effect in the House in which the Bill comes up for consideration. In case the motion adopted is for reference of the Bill to a Joint Committee, the decision is conveyed to the other House requesting them to nominate members of the other House to serve on the Committee. The Select or Joint Committee considers the Bill clause by clause just as the two Houses do. Amendments can be moved to various clauses by members of the Committee. The Committee can also take evidence of associations, public bodies or experts who are interested in the Bill. After the Bill has thus been considered the Committee submits its report to the House. Members who do not agree with the majority report may append their minutes of dissent to the report.</p>
<p><strong>Committee on Estimates</strong></p>
<p>This Committee consists of 30 members who are elected by the Lok Sabha every year from amongst its members. A Minister is not eligible for election to this Committee. The term of the Committee is one year. The main function of the Committee on Estimates is to report what economies, improvements in organization, efficiency, or administrative reform, consistent with the policy underlying the estimates may be effected and to suggest alternative policies in order to bring about efficiency and economy in administration. From time to time the Committee selects such of the estimates pertaining to a Ministry or a group of Ministries or the statutory and other Government bodies as may seem fit to the Committee. The Committee also examines matters of special interests which may arise or come to light in the course of its work or which are specifically referred to it by the House or the Speaker.</p>
<p><strong>Committee on Public Undertakings</strong></p>
<p>The Committee on Public Undertaking consists of 15 members. 10 members are elected by the Lok Sabha and 5 members of Rjya Sabha are associated with it. A Minister is not eligible for election to this Committee. The term of the Committee is one year. The functions of the Committee on Public Undertakings are:</p>
<ul>
    <li>To examine the reports and accounts of Public Undertakings are;</li>
    <li>To examine the reports, if any, of the Comptroller and Auditor General on the Public Undertakings;</li>
    <li>To examine in the context of the autonomy and efficiency of the Public Undertakings whether the affairs of the Public Undertakings are being managed in accordance with sound business principles and prudent commercial practices; and</li>
    <li>Such other functions vested in the Committee on Public Accounts and the Committee on Estimates in relation to the Public Undertakings as are not covered by clauses (a), (b) &amp; (c) above and as may be allotted to the Committee by the Speaker from time to time.</li>
</ul>
<p>The Committee does not, however, examine matters of major Government policy and matters of day-to-day administration of the Undertakings. The Committee is headed by a member of Lok Sabha who is from the Opposition parties.</p>
<p><strong>Committee on Public Accounts</strong></p>
<p>This Committee consists of 15 members elected by the Lok Sabha and 7 members of the Rajya Sabha are associated with it. A Minister is not eligible for election to this Committee. The term of the Committee is one year.&nbsp;</p>
<p>The main duty of the Committee is to ascertain whether the money granted by Parliament has been spent by Government within the scope of the Demand. The Appropriation Accounts of the Government of India and the Audit Reports presented by the Comptroller and Auditor General mainly from the basis for the examination of the Committee. Cases involving losses, nugatory expenditure and financial irregularities come in for severe criticism by the Committee. &nbsp;The Committee is not concerned with questions of policy. It is concerned only with the execution of the policy laid down by Parliament and its results.</p></div>
