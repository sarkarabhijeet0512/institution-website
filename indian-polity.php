  <div class="menu-content Supreme"><p><strong>Supreme Court</strong></p>
<p>The Supreme Court of India is the highest court of the land as established by Part V, Chapter IV of the Constitution of India. According to the Constitution of India, the role of the Supreme Court of India is that of a federal court, guardian of the Constitution and the highest court of appeal.&nbsp;</p>
<p>According to <strong>Article 124(2)</strong>, every Judge of the Supreme Court shall be appointed by the President by warrant under his hand and seal after consultation with such of the Judges of the Supreme Court and of the High Courts in the States as the President may deem necessary for the purpose and shall hold office until he attains the age of sixty-five years:</p>
<p>Provided that in the case of appointment of a Judge other than the Chief Justice, the Chief Justice of India shall always be consulted.</p>
<p><strong>Qualification of the Judges</strong></p>
<p>According to Article 124(3), a person shall not be qualified for appointment as a Judge of the Supreme Court unless he is a citizen of India and</p>
<ul>
    <li>Has been for at least five years a Judge of High Court or of two or more such Courts in succession; or&nbsp;</li>
    <li>Has been for at least ten years an advocate of a High Court or of two or more such Courts in succession; or</li>
    <li>In the opinion of the President, a distinguished jurist.</li>
</ul>
<p><strong>Supreme Court to be a Court of Record</strong></p>
<p>According to <strong>Article 129</strong>, the Supreme Court shall be a court of record and shall have all the powers of such a court including the power to punish for contempt of itself.</p>
<p><strong>Seat of Supreme Court</strong></p>
<p>According to Article 130, the Supreme Court shall sit in Delhi or in such other place or places, as the Chief Justice of India may, with the approval of the President, from time to time appoint.</p>
<p><strong>Removal of the Judges of Supreme Court</strong></p>
<p><strong>Article 124(4)</strong> provides for the removal of a Judge of Supreme Court. It states that a Judge of the Supreme Court is removed by the President upon the passage of resolution by both the Houses of the Parliament supported by not less than 2/3 of the members present and voting and by a majority of the House on the grounds of proved misbehavior or incapacity.</p>
<p><strong>Article 124(4)</strong> is an incomplete provision. <strong>Article 124(5)&nbsp;</strong>authorizes the Parliament to regulate by law providing for the procedure for the introduction of the resolution and the proof of misbehavior or incapacity. Accordingly, the Parliament enacted the Judges (Inquiry) Act, 1968. The Act contains the procedure to be adopted for the passage of resolution in the Parliament.</p>
<p>The resolution can be introduced in either House of the Parliament. However, for its introduction in the Lok Sabha , it should be supported by not less than 100 members of the House and if it is introduced in the Rjya Sabha, it must be supported by not less than 50 members of the House. After its proper introduction the presiding officer shall appoint a Judicial Committee to investigate charges against the Judge. The committee is headed by a sitting Judge of the Supreme Court. The second member can be a sitting Judge of either the Supreme Court or High Court and the third member can be an eminent jurist. The concerned Judge has the right to defend himself before the judicial committee. After the presentation of the report of the Committee, both the Houses can pass the resolution by the majority mentioned under Article 124(4). On the passage of the resolution, the President can remove the concerned Judge.</p>
<p><strong>Jurisdiction of the Supreme Court&nbsp;</strong></p>
<ul>
    <li><strong>Original:&nbsp;</strong>Its exclusive original jurisdiction extends to any dispute between the Government of India and one or more States or between the Government of India and any State or States on one side and one or more States on the other or between two or more States, if and insofar as the dispute involves any question (whether of law or of fact) on which the existence or extent of a legal right depends.</li>
    <li><strong>Appellate Jurisdiction:&nbsp;</strong>The appellate jurisdiction of the Supreme Court can be invoked by a certificate granted by the High Court concerned under Article 132(1), 133(1) or 134 of the Constitution in respect of any judgement, decree or final order of a High Court in both civil and criminal cases, involving substantial questions of law as to the interpretation of the Constitution.</li>
    <li><strong>Writ:&nbsp;</strong>In addition, Article 32 of the Constitution gives an extensive original jurisdiction to the Supreme Court in regard to enforcement of Fundamental Rights. It is empowered to issue directions, orders or writs, including writs in the nature of habeas corpus, mandamus, prohibition, quo warranto and certiorari to enforce them.&nbsp;</li>
</ul>
<p><strong>Advisory:&nbsp;</strong>The Supreme Court has special advisory jurisdiction in matters which may specifically be referred to it by the President of India under Article 143 of the Constitution. There are provisions for reference or appeal to this Court under Article 317(1) of the Constitution, Section 257 of the Income Tax Act 1961, Section 7(2) of the Monopolies and Restrictive Trade Practices Act, 1969, Section 130-A of the Customs Act, 1962, Section 35-H of the Central Excises and Salt Act, 1944 and Section 82C of the Gold (Control) Act, 1968.</p></div>
  <div class="menu-content Review"><h1>THE JUDICIARY</h1><hr>
<p>Courts take decisions on a very large number of issues. They can decide that no teacher can beat a student, or about the sharing of river waters between states, or they can punish people for particular crimes. Broadly speaking, the work that the judiciary does can be divided into the following:</p>
<ul>
    <li>Dispute Resolution between Centre and States; State and States.</li>
    <li>As the final interpreter of the Constitution, the judiciary also has the power to strike down particular laws passed by the Parliament if it believes that these are a violation of the basic structure of the Constitution. This is called <strong>judicial review</strong>.</li>
    <li>&nbsp;Upholding the Law and Enforcing Fundamental Rights.</li>
</ul>
<p><strong>An Independent Judiciary</strong></p>
<p>The control that the politician holds over the Judge does not allow for the Judge to take an independent decision. This lack of independence would force the Judge to make all judgments in favour of the politician. Although we often hear of rich and powerful people in India trying to influence the judicial process, the Indian Constitution protects against this kind of situation by providing for the independence of the judiciary.</p>
<p>One aspect of this independence is the &lsquo;separation of power&rsquo;. What this means here is that other branches of the State - like the legislature and the executive &ndash; cannot interfere in the work of the judiciary. The courts are not under the government and do not act on their behalf.</p>
<p>For the above separation to work well, it is also crucial that all judges in the High Court as well as the Supreme Court are appointed with very little interference from these other branches of government. Once appointed to this office, it is also very difficult to remove a Judge. It is the independence of the judiciary that allows the courts to play a central role in ensuring that there is no misuse of power by the legislature and the executive. It also plays a crucial role in protecting the Fundamental Rights of citizens because anyone can approach the courts if they believe that their rights have been violated.</p>
<p><strong>The Structure of Courts in India</strong></p>
<p>There are three different levels of courts in our country. There are several courts at the lower level while there is only one at the apex level. The courts that most people interact with are what are called subordinate or district courts. These are usually at the district or Tehsil level or in towns and they hear many kinds of cases. Each state is divided into districts that are presided over by a District Judge. Each state has a High Court which is the highest court of that state. At the top is the Supreme Court that is located in New Delhi and is presided over by the Chief Justice of India. The decisions made by the Supreme Court are binding on all other courts in India.</p>
<p>In India, we have an integrated judicial system, meaning that the decisions made by higher courts are binding on the lower courts. Another way to understand this integration is through the appellate system that exists in India. This means that a person can appeal to a higher court if they believe that the judgement passed by the lower court is not just.</p>
