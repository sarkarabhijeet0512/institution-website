  <div class="menu-content Making"><h1>CONSTITUENT ASSEMBLY</h1>
<hr>
<p><strong>Composition of the Constituent Assembly</strong></p>
<p>The Constituent Assembly was constituted in November 1946 under the Scheme formulated by the Cabinet Mission Plan. The Mission recommended that the representation of the provinces in the constitution-making body be on the basis of population, roughly in the ratio of one Member to a million. The seats allocated to the provinces be divided among the principal communities, classified for this purpose as Sikhs, Muslims and General (all except Sikhs and Muslims), on the basis of their numerical strength. The representatives of each community were to be chosen by members of that community in the provincial assembly and voting was to be by the method of proportional representation with single transferable vote. The number of members allotted to the Indian State was also to be fixed on the same basis of population as adopted for British India, but the method of their select ion was to be settled later by consultation.</p>
<p>The strength of the constitution-making body was to be 389. Of these 296 representatives were to be from British India (292 representatives drawn from the eleven Governor&rsquo;s Provinces of British India and a representative each from the four Chief Commissioners&rsquo; Provinces of Delhi, Ajmer-Marwara, Coorg and British Baluchistan) and 93 representatives from the Indian States.</p>
<p><strong>Working of the Constituent Assembly</strong></p>
<p>The Constituent Assembly duly opened on the ninth day of December 1946 at eleven in the morning. The Muslim League members did not attend, and the Constituent Assembly began to function with the Non-Muslim League members.</p>
<p>According to June 3, 1947 Plan, the representatives of the Muslim majority areas of the two Provinces (i.e., West Punjab and East Bengal) voted for partition and for joining a new Constituent Assembly. The referendum in the North Western Frontier and Sylhet was in favour of Pakistan. On the 26th July, 1947, the Governor-General announced the sitting up of a separate Constituent Assembly for Pakistan.</p>
<p>Following the members of the Muslim League Party from the Indian Dominion also took their seats in the assembly. The representative of some of the Indian states had already entered the assembly on 28th April, 1947. By 15August, 1947 most of the States were represented in the assembly and the remaining states also sent their representatives in due course.</p>
<p>The first meeting of the Constituent Assembly was attended by only 211 members.&nbsp;</p>
<p>Dr. Sachchidanand Sinha was elected as the Temporary President of the Assembly.</p>
<p>On December 11, 1946, Dr. Rajendra Prasad and H.C. Mukherjee were elected as the President and Vice-President of the Assembly respectively.&nbsp;</p>
<p>Sir B.N.&nbsp; Rau was appointed as the Constitutional Adviser to the Assembly.</p>
<p><strong>Objectives of Resolution</strong></p>
<p>The historic Objectives Resolution was moved in the Constituent Assembly by Nehru, on 13th December, 1946. It laid down the fundamentals and philosophy of the constitutional structures.</p>
<p><strong>Framing the Constitution</strong></p>
<p>The Constitution Assembly appointed a number of Committees on procedural and substantive matters to deal with different aspects of the problem of framing the constitution. Out of these, eight were major committees and the others were minor committees. The report of these committees formed the basis on which the first draft of the Constitution was prepared. These were as follows:</p>
<p><strong>Major Committees</strong></p>
<ul>
    <li>Union Powers Committee, Chairman &ndash; Jawaharlal Nehru</li>
    <li>Union Constitution Committee, Chairman - Jawaharlal Nehru</li>
    <li>Provincial Constitution Committee, Chairman &ndash; Sardar Patel</li>
    <li>Drafting Committee, Chairman &ndash; Dr. B.R. Ambedkar</li>
    <li>Advisory Committee on Fundamental Rights and Minorities, Chairman &ndash; Sardar Patel. This committee had two sub-committees:</li>
</ul>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&rarr;Fundamental Rights Sub-Committee, Chairman &ndash; J.B. Kripalani</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&rarr;Minorities Sub-Committee, Chairman- H.C. Mukherjee</p>
<ul>
    <li>Rules of procedure Committee, Chairman &ndash; Dr. Rajendra Prasad</li>
    <li>States Committee (Committee for Negotiating with States), Chairman- Jawaharlal Nehru</li>
    <li>Steering Committee, Chairman &ndash; Dr. Rajendra Prasad</li>
</ul>
<p><strong>Drafting Committee</strong></p>
<p>Most of the Committees worked as standing Committees. Only the Drafting Committee functioned till the end of the constitution making. It was this committee that was entrusted with the task of preparing a draft of the new Constitution. This committee had the following members:</p>
<ul>
    <li>Dr. B.R. Ambedkar (Chairman)</li>
    <li>N. Gopalaswamy Ayyangar</li>
    <li>Alladi Krishnaswamy Ayyar</li>
    <li>Dr. K.M. Munshi</li>
    <li>Syed Mohammad Saadullah</li>
    <li>N. Madhava Rau (He replaced B.L. Mitter who resigned due to ill health)</li>
    <li>T.T. Krishnamachari (He replaced D.P. Khaitan who died in 1948)</li>
</ul>
<p><strong>Other Functions Performed</strong></p>
<p>Besides framing the Constitution and enacting of ordinary laws, the Constituent Assembly performed several other important functions:</p>
<ul>
    <li>It adopted the National Flag on July 22, 1947.</li>
    <li>It adopted the National Anthem on January 24, 1950.</li>
    <li>It adopted the National Song on January 24, 1950.</li>
    <li>It ratified the India&rsquo;s membership of the Commonwealth in May 1949.</li>
    <li>It elected Dr. Rajendra Prasad as the first President of India on January 24, 1950.</li>
</ul>
<p><strong>The Constituent Assembly completed the Constitution in two years, 11months and 18 days.</strong></p>
<p>On January 24, 1950, the Constituent Assembly held its final session. It, however, did not end, and continued as the provisional parliament of India from January 26, 1950 till the formation of new Parliament after the first general elections in 1951-52.</p>
