  <div class="menu-content President"><h1>President</h1>
<p><strong>Qualifications for Election as President</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Article 58(1):&nbsp;</strong>No person shall be eligible for election as President unless he:</p>
<ul>
    <li>Is a citizen of India,</li>
    <li>Has completed the age of thirty- five years, and&nbsp;</li>
    <li>Is qualified for election as a member of the House of the People.</li>
</ul>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; Article 58(2):&nbsp;</strong>States that a person shall not be eligible for election President if he holds any office of profit under the Government of India or the Government of any State or under any local or other authority subject to the control of any of the said Governments.</p>
<p><strong>Election of the President</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Articles 54, 55 and 71&nbsp;</strong>of the Constitution deal with the election of the President. Apart from these provisions the Parliament has also enacted certain laws regulating the election of the President. The <strong>nominated members and the MLCs do not participate</strong> in the presidential election.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Article 54 reads &ndash; The President shall be elected by the members of an electoral college consisting of:</p>
<ul>
    <li>The elected members of both Houses of Parliament; and</li>
    <li>The elected members of the Legislative Assemblies of the States.</li>
</ul>
<p>&nbsp; &nbsp; &nbsp; &nbsp; The 70<sup>th</sup> Constitutional (Amendment) Act, 1992 gave voting rights to MLA&rsquo;s of Delhi and Puducherry for election of President. Thus in this Article &ldquo;State&rdquo; includes the National Capital Territory of Delhi and the Union Territory of Puducherry.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; <strong>Article 55</strong> deals with the method of the President&rsquo;s election. The President is elected by Proportional Representation with single transferable vote system. Proportional representation means that in order to get elected the candidate must get quota of votes i.e., more than 50% of valid votes. Under the Single Transferable Vote system an elector can indicate more than one preference on his ballot paper. He can make as many preferences as the number of candidates are. However, he must not mention all preferences. At least one preference must be made for the validity of votes.</p>
<p><strong>Values of vote of one MLA</strong>= Total Population of the States/ total number of elected MLAs * 1/1000.</p>
<p><strong>&nbsp;Values of vote of one MP</strong>= Total value of votes of all the MLAs/Total Number of elected MPs.</p>
<p>According to <strong>Article 71(1)</strong>, all doubts and disputes arising out of or in connection with the election of a President or Vice-President shall be inquired into and decided by the Supreme Court whose decision shall be final.</p>
<p><strong>Article 71(2)</strong> states that if the election of a person as President or Vice- President is declared void by the Supreme Court, acts done by him in the exercise and performance of the powers and duties of the office of President or Vice- President, as the case may be, on or before the date of the decision of the Supreme Court shall not be invalidated by reason of that declaration.</p>
<p><strong>Term of office of the President</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</strong>The President shall hold office for a term of five years from the date on which he enters upon his office but he is eligible for re-election.</p>
<ul>
    <li>The President may, by writing under his hand addressed to the Vice- President, resign his office;</li>
    <li>The President may, for violation of the Constitution, be removed from office by impeachment in the manner provided in Article 61;</li>
    <li>The President shall, notwithstanding the expiration of his term, continue to hold office until his successor enters upon his office.</li>
</ul>
<p>&nbsp; &nbsp; &nbsp; &nbsp;According to <strong>Article 56(2)</strong> any resignation addressed to the Vice-President under clause (a) of the provision to clause (1) shall forthwith be communicated by him to the Speaker of the House of the People.</p>
<p><strong>Powers and Position of President</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</strong>The powers and functions that have been vested in the President of India may be classified under the following heads: executive powers, legislative powers, military powers, diplomatic powers, judicial powers, financial powers and emergency powers.</p>
<p><strong>Executive Powers</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Article 53</strong> of the constitution lays down that the executive power of the Union shall be vested in the President thus making him the head of the executive. All executive actions of the Union must be expressed to be taken in the name of the President and as such he shall have a right to be informed of the affairs of the Union. The executive power includes the power to appoint and remove the high constitutional authority of the country. Under this power he appoints the Prime Minister and other members of the Council of Ministers and distributes portfolios among them. He also appoints the judges of the Supreme Court and the High Court in the States.</p>
<p><strong>Legislative Powers</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</strong>The President of India is an integral part of the Union Parliament. The Parliament cannot function without involving him. He alone can summon and prorogue the Houses of Parliament. If there is a deadlock in the process of legislation between both the Houses of Parliament, the President has the power to summon a joint session to do away with the impasse. He addresses both the Houses on the inaugural session after each general election and presents the agenda of the government. Apart from this he can send messages to either Houses of Parliament on any important matter of national, constitutional or public interest. The constitution requires the previous sanction of the President for introducing certain legislations for example a bill which seeks to create a new state or change the boundary of an existing state or change the name of a state or dealing with certain matters relating of Fundamental Rights. Money bills also require the previous sanction of the President before its introduction in the Lower House. Besides this all bills after they are passed in the Parliament need his consent to become Acts.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;The President can also promulgate ordinances during the recess of the Parliament. Ordinance is an emergent legislation.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Further he causes some important reports to be submitted on the floor of the Lok Sabha. The reports of Comptroller and Auditor General, Finance Commission, Union Public Service Commission, Language Commission are some of the many reports caused by the President to be laid before the Parliament.</p>
<p><strong>Military Powers</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp;&nbsp;</strong>The Constitution vests the Supreme Command of the Defence Force in the President of India. As such he makes all important appointments in the Defence Force including the Chief of the three wings: the Armed Force, the Air Force and the Naval Force. He can declare war and conclude peace. However, the President cannot do so himself. Under the Constitution he is required to do so under the control and authority of the Parliament.</p>
<p><strong>Judicial Powers</strong></p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; The President of India as head of the executive enjoys some judicial powers like most other heads of democratic countries the world over. The purpose of granting such powers to the President is to rectify the possible judicial errors. No human system of judicial administration can ever be foolproof. The president of India can grant pardons, reprieves or respites or remission of punishment. He can suspend, remit or commute the sentence of any person convicted of any offence in cases where the punishment is by a court martial or death sentence.</p>
<p><strong>Financial Powers</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</strong>With regard to his powers in the field of finance the constitution provides that all money bills will need his consent to be initialed in the Lower House of Parliament. He causes the budget to be laid on the floors of Lok Sabha every financial year. He is empowered to order for the presentation of the report of the Auditor General of India relating to the accounts of the Government of India. The Finance Commission submits its report containing its recommendations and actions taken for his placement in the legislature.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;The Contingency Fund of India is at his disposal. He can make advances out of it to meet the unforeseen expenditure.</p>
<p><strong>Emergency Powers</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</strong>The makers of Indian Constitution were influenced by the relevant provisions of the Government of India Act, 1935 and the Constitution of Weimer Republic of Germany where such provisions had been incorporated. In the Constitution of India three kinds of emergencies have been envisaged such as National Emergency, State Emergency and Financial Emergency.</p>
<p><strong>>Discretionary Powers of President</strong></p>

<p><strong>Appointment of Prime Minister</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</strong>In normal circumstances, after the general elections, when a pre-poll coalition or a single party gets majority in the Lok Sabha then the President is bound to invite the leader of that coalition or the political Party to form a government but when there is hung Parliament President have the discretion to call upon the leader who he thinks can form the Government.</p>
<p><strong>Power to send a Bill for the Reconsideration of Parliament</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</strong>The President can send a bill other than a money bill or a constitutional amendment bill for the reconsideration of Parliament only for once. The former President Abdul Kalam sent the Office of Profit bill for the reconsideration of Parliament.</p>
<p><strong>Pocket Veto</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</strong>When an ordinary bill after being passed by the Parliament goes to the President for his/her assent, he/she can give his/her assent or he/she may declare that he/she is withholding the assent, or he/she may send the bill for the reconsideration of Parliament only for once.</p>
<p><strong>Dissolution of the Lok Sabha</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;</strong> In normal circumstances the President dissolves the Lok Sabha on the aid and advice of the Council of Ministers but he is not bound to follow the advice of the Council of Minsters which has lost the vote of confidence in the lower house. Here the President will look that whether an alternative government can be formed or not.</p>
<p><strong>Impeachment of the President&nbsp;</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Article 61&nbsp;</strong>deals with the impeachment of the President. The President can be impeached on the grounds of violation of the Constitution. The impeachment motion can be brought about in the form of resolution in either House of the Parliament. It must be supported by not less than &frac14; of the total strength of the House for its introduction. This resolution can be passed with a special majority of not less than 2/3 of the total strength of the House voting in favour of the impeachment motion. It can be passed only after the expiry of the 14 days notice served to the President.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;In the meantime the other House acts as an investigating House. It investigates the charges itself or appoints a committee to investigate. The President has the right to defend himself in this House through an attorney of his choice or personally. If the second House also passes the resolution after investigation by not less than 2/3 of the total strength of the House, then from that day the President is considered to be impeached. It is s quasi-judicial process as the second House acts as the investigating House.</p>
<p><strong>Privileges of the President according to Article 361</strong></p>
<ul>
    <li>The President or the Governor or Rajpramukh of a State, shall not be answerable to any court for the exercise and performance of the powers and duties of his office or for any act done or purporting to be done by him in the exercise and performance of those powers and duties: Provided that the conduct of the President may be brought under review by any court, tribunal or body appointed or designated by either House of Parliament for the investigation of a charge under Article 61.&nbsp;</li>
    <li>No criminal proceedings whatsoever shall be instituted or continued against the President, or the governor of a State, in any court during his term of office.</li>
    <li>No process for the arrest or imprisonment of the President, or the Governor of a State, shall issue from any court during his term of office.</li>
    <li>No civil proceedings in which relief is claimed against the President, or the Governor of a State.</li>
</ul>
<p><strong>National Emergency (Article 352)</strong></p>
<ul>
    <li>It can be proclaimed only when the security of Indian or a part of it is threatened by war, external aggression or armed rebellion. Its effect is that the Centre gets concurrent powers of administration and legislation in the State. Powers of the State are assumed by the Centre.</li>
    <li>Under this, the Parliament can made laws on the subjects enumerated in the State List only by itself. That is, it cannot delegate the same to any other body or authority.</li>
    <li>There is no maximum period prescribed for its operation. It can be continued indefinitely with the approval of Parliament for every six months.</li>
    <li>Every resolution of Parliament approving its proclamation or its continuance must be passed by a special majority.&nbsp;</li>
    <li>Lok Sabha can pass a resolution for its revocation.</li>
</ul>

