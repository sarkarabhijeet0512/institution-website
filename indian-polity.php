  <div class="menu-content Fundamental"><h1>FUNDAMENTAL RIGHTS</h1><hr>
<p>Rights are necessary for the very sustenance of a democracy. In a democracy every citizen has to have the right to vote and the right to be elected to government. For democratic elections to take place it is necessary that citizens should have the right to express their opinion, form political parties and take part in political activities. Rights also perform a very special role in a democracy. Rights protect minorities from the oppression of majority. They ensure that the majority cannot do whatever it likes. Rights are guarantees which can be used when things go wrong. In most democracies the basic rights of the citizen are written down in the constitution. Constitution provides for <strong>six Fundamental Rights</strong>.</p>
<div>
    <ol>
        <li><strong>Right to Equality and Constitutional Provisions</strong></li>
    </ol>
</div>
<p>&nbsp; &nbsp; &nbsp; &nbsp;<strong>Article 14&nbsp;</strong>provides for Equality before law. The State shall not dent to any person equality before the law or the equal protection of the laws within the territory of India. Equality before law is a negative concept in the parlance of governance because it forces a state to provide these rights and in case state defaults then there are judicial safeguards for the citizens to claim their rights. It is based on the concept of Rule of law. The concept has been borrowed from United Kingdom&rsquo;s Constitution. It signifies that no one is above the law.</p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;Article 15&nbsp;</strong>provides for prohibition of discrimination on grounds of religion, race, caste, sex or place of birth. The State shall not discriminate against any citizen on grounds only of religion, race, caste, sex and place of birth or any of them. This right is available only to citizens. But there is an exception to it. Exception as is provided in Article 15(3) and Article 15(4). Article 15(3) says that nothing in this article shall prevent the State from making any special provision for women and children. Article 15(4) says that nothing in this article or in clause (2) of Article 29 shall prevent the State from making any special provision for the advancement of any socially and educationally backward classes of citizens or for the Schedule Caste and the Schedule Tribes.</p>
<p><strong> &nbsp;&nbsp; &nbsp; Article 16&nbsp;</strong>provides for <strong>equality of opportunity in matters of public employment.&nbsp;</strong>There shall be equality of opportunity for all citizens in matters relating to employment or appointment to any office under the State. No citizen shall, on grounds only of religion, race, caste, sex, descent, place of birth, residence or any of them, be ineligible for, or discriminated against in respect of, any employment or office under State. This right is available only to citizens. There are few exceptions to it. Article 16(3) says nothing in this article shall prevent Parliament from making any law prescribing, in regard to a class or classes of employment or appointment to an office under the Government of, or any local or other authority within, a State or Union Territory, any requirement as to residence within that State or Union Territory prior to such employment or appointment. Article 16(4) says nothing in this article shall prevent the State from making any provision for the reservation of appointments or posts in favour of any backward class of citizens which, in the opinion of the State, is not adequately represented in the services under the State.</p>
<p>&nbsp; &nbsp; &nbsp; Furthermore, there is Article 16(4A). It states that nothing in this article shall prevent the State from making any provision for reservation in matters of promotion, with consequential seniority, to any class or classes of posts in the services under the State in favour of the Scheduled Caste and Scheduled Tribes which, in the opinion of the State, are not adequately represented in the services under the State.</p>
<p> &nbsp; &nbsp; &nbsp; <strong>Article 17</strong> provides for <strong>abolition of untouchability</strong>. It is abolished and its practice in any form is forbidden. The enforcement of any disability arising out of untouchability shall be an offence punishable in accordance with law. The Parliament enacted the Untouchability (Offences) Act, 1955. This act was amended by the Untouchability (Offences) Act, 1976, in order to make the law more stringent to remove the evil of untouchability from the society. It has been renamed as, The Protection of Civil Rights Act, 1955. The Protection of Civil Rights Act prescribes punishment which may extend to imprisonment upto six months and also with a fine which may extend to five hundred rupees or both.</p>
<p><strong> &nbsp; &nbsp; &nbsp;Article 18&nbsp;</strong>provides for <strong>abolition of titles</strong>. Article 18 is not Penal in return but only directly in nature.</p>
<div>
    <ol>
        <li><strong>Right to Freedom</strong></li>
    </ol>
</div>
<p> &nbsp; &nbsp; &nbsp;Freedom means absence of constraints. In practical life it means absence of interference in our affairs by others be it other individuals or the government. We want to live in society, but we want to be free. We want to do things in the way we want to do them. Others should not dictate us what we should do.</p>
<p><strong>Right to Freedom in Indian Institution under Article 19</strong></p>
<ul>
    <li>Freedom of speech and expression&nbsp;</li>
    <li>Assembly in a peaceful manner</li>
    <li>Form associations and unions or cooperatives</li>
    <li>Move freely throughout the country</li>
    <li>Reside in any part of the country</li>
    <li>Practice any profession or to carry on any occupation, trade or business&nbsp;</li>
</ul>
<p><strong>Freedom of Speech and Expression: Scope and Content</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</strong>The right to freedom of speech and expression has been subjected to wide interpretations by the Supreme Court.</p>
<ul>
    <li>Freedom of Press</li>
    <li>Right to know and to obtain information</li>
    <li>Right of citizens/ voters to know the antecedents of the candidates at the time of election</li>
    <li>Right to reply or answer criticism against one&rsquo;s views</li>
    <li>Demonstrations and Picketing</li>
    <li>No Right to call or enforce band, hartals, blockades</li>
    <li>Freedom of Silence</li>
    <li>Right of a convict to express himself</li>
</ul>
<p><strong>Reasonable Restrictions</strong></p>
<ul>
    <li>In the interest of the security of country</li>
    <li>Friendly relations with foreign States</li>
    <li>For the maintenance of public order, morality and decency</li>
    <li>On the grounds of contempt of Court and defamation&nbsp;</li>
    <li>Instigating violence</li>
</ul>
<p><strong>Protection in Respect of Conviction for Offences</strong></p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; <strong>Article 20</strong> of the Constitution says that no person shall be convicted of any offence except for violation of a law in force at the time of the commission of the Act charged as an offence, nor be subjected to a penalty greater than that which might have been inflicted under the law in force at the time of the commission of the offence. No person shall be prosecuted and punished for the same offence more than once. No person accused of any offence shall be compelled to be a witness against himself. Available to all individuals and cannot be suspended even during emergency.</p>
<p><strong>Right to Life and Personal Liberty</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Article 21&nbsp;</strong>of the Constitution says that no person can be deprived of his life or personal liberty except according to procedure established by law. It means that no person can be killed unless the court has ordered a death sentence. Available to all individuals and cannot be suspended even during emergency. Even when they do, they have to follow some procedures:</p>
<ul>
    <li>A person who is arrested and detained in custody will have to be informed of the reasons for such arrest and detention.</li>
    <li>A person who is arrested and detained shall be produced before the nearest magistrate within a period of 24 hours of arrest.</li>
    <li>Such a person has the right to consult a lawyer or engage a lawyer for his defence.</li>
</ul>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Supreme Court has extended the scope of &ldquo;Right to life&rdquo; by making Article 21 repositories of all other important rights that are not explicitly mentioned in the constitution such as Right to dignified life, Right to privacy, Right to clean environment etc.</p>
<p><strong>Protection against Arrest and Detention</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Article 22&nbsp;</strong>of the Constitution says that no person who is arrested shall be detained in custody without being informed, as soon as possible, of the grounds for such arrest nor shall he be denied the right to consult, and to be defended by, a legal practitioner of his choice. Every person who is arrested and detained in custody shall be produced before the nearest magistrate within a period of twenty-four hours of such arrest excluding the time necessary for the journey from the place of arrest to the court of the magistrate and no such person shall be detained in custody beyond the said period without the authority of a magistrate.</p>
<p><strong>Exceptions and Preventive Detention</strong></p>
<p> &nbsp; &nbsp; &nbsp; According to Article 22 of the Constitution these rights are not available to the enemy aliens and secondly the persons detained under the preventive detention laws. Preventive detention means the detention of a person without trial in such circumstances that the evidence in possession of the authority is not sufficient to make a legal charge.</p>
<div>
    <ol>
        <li><strong>Right against Exploitation</strong></li>
    </ol>
</div>
<p><strong>&nbsp; &nbsp; &nbsp;</strong>Once the right to liberty and equality is granted, it follows that every citizen has a right not to be exploited. Yet the Constitution makers thought it was necessary to write down certain clear provisions to prevent exploitation of the weaker sections of the society. The Constitution mentions three specific evils and declares these illegal. In Article 23 first, the Constitution prohibits &lsquo;traffic in human beings&rsquo;. Traffic here means selling and buying of human beings, usually women, for immoral purposes. Second, our Constitution also prohibits forced labour or beggar in any form.</p>
<p> &nbsp; &nbsp; &nbsp; &lsquo;Begar&rsquo; is a practice where the worker is forced to render service to the &lsquo;master&rsquo; free of charge or at a nominal remuneration. When this practice takes place on a life-long basis, it is called the practice of bonded labour.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Finally, in Article 24, the Constitution also prohibits child labour. No one can employ a child below the age of fourteen to work in any factory or mine or in any other hazardous work. Such as railways and ports. Using this as a basis many laws have been made to prohibit children from working in industries such as beedi making, firecrackers and matches, printing and dyeing.</p>
<div>
    <ol>
        <li><strong>Right to Freedom of Religion</strong></li>
    </ol>
</div>
<p> &nbsp; &nbsp; &nbsp; &nbsp;Article 25 of the Constitution gives freedom of conscience. It guarantees practice and propagation of religion. But it is subject to public order, morality and health and to the other provisions of this Part, all persons are equally entitled to freedom of conscience and the right freely to profess, practice and propagate religion.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp;Article 26 assures freedom to manage religious affairs. Subject to public order, morality and health, every religious denomination or any section thereof shall have the right:</p>
<ul>
    <li>To establish and maintain institutions for religious and charitable purposes;</li>
    <li>To manage its own affairs in matters of religion;</li>
    <li>To own and acquire movable and immovable property; and</li>
    <li>To administer such property in accordance with law.</li>
</ul>
<p>&nbsp; &nbsp; &nbsp; &nbsp; However, the affair of these institutions can be regulated for the maintenance of public order, decency etc.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp;Article 27 gives freedom as to payment of taxes for promotion of any particular religion. It says that no person shall be compelled to pay any taxes, the proceeds of which are specifically appropriated in payment of expenses for the promotion or maintenance of any particular religion or religious denomination.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; Article 28 gives freedom as to attendance at religious instruction or religious worship in certain educational institutions.</p>
<ul>
    <li>No religious instruction shall be provided in any educational institutions wholly maintained out of State funds.</li>
    <li>Nothing in above shall apply to an educational institution which is administered by the State but has been established under any endowment or trust which requires that religious instruction shall be imparted in such institution.</li>
    <li>No person attending any educational institution recognized by the State or receiving and out of State funds shall be required to take part in any religious instruction that may be imparted in such institution or to attend any religious worship that may be conducted in such institution or in any premises attached thereto unless such person or, if such person is a minor, his guardian has given his consent thereto.</li>
</ul>
<p>&nbsp;</p>
<div>
    <ol>
        <li><strong>Cultural and Educational Rights</strong></li>
    </ol>
</div>
<p>It is the language, culture and religion of minorities that needs special protection. Otherwise, they may get neglected or undermined under the impact of the language, religion and culture of the majority. That is the way the Constitution under Article 29 specifies the cultural and educational right of the minorities:</p>
<ul>
    <li>Any section of citizens with a distinct language or culture has a right to conserve it.</li>
    <li>Admission to any educational institution maintained by government or receiving government aid cannot be denied to any citizen on the ground of religion or language.</li>
    <li>All minorities have the right to establish and administer educational institutions of their choice.</li>
</ul>
<p>Here minority does not means both linguistic and religious.</p>
<p> &nbsp; &nbsp; &nbsp; &nbsp;Article 30 provides for a right of minorities to establish and administer educational institutions. It states that all minorities, whether based on religion or language, shall have the right to establish and administer educational institutions of their choice. Further Article 30(2) provides that the State shall not in granting aid to educational institutions, discriminate against any educational institution on the ground that it is under the management of a minority, whether based on religion or language.</p>
<p> &nbsp; &nbsp; &nbsp;These rights are also known as minority rights. The objective behind Article 29 and 30, in the opinion of the Supreme Court, is the recognition and preservation of the different types of people with diverse languages and different beliefs which constitute the essence of secularism in India.</p>
<div>
    <ol>
        <li><strong>Right to Constitutional Remedies&nbsp;</strong></li>
    </ol>
</div>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;</strong>One would agree that our Constitution contains a very impressive list of Fundamental Rights. But merely writing down a list of rights is not enough. There has to be a way through which they could be realized in practice and defended against any attack on these rights. Right to constitutional remedies is the means through which this is to be achieved. Dr. Ambedkar considered the right to constitutional remedies as &lsquo;heart and soul of the constitution&rsquo;. It is so because this right gives a citizen the right to approach a High Court or Supreme Court to get any of the fundamental rights restored in case of their violation. The Supreme Court and the High Courts can issue orders and give directives to the government for the enforcement of rights. The courts can issue various special orders known as writs.</p>
<p><strong>Writs</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp;Habeas corpus:&nbsp;</strong>A writ of habeas corpus means that the court orders that the arrested person should be presented before it. It can also order to set free an arrested person if the manner or grounds of arrest are not lawful or satisfactory.&nbsp;</p>
<p>&nbsp; &nbsp; &nbsp;<strong>Mandamus:&nbsp;</strong>This writ is issued when the court finds that a particular office holder is not doing legal duty and thereby is infringing on the right of an individual.</p>
<p>&nbsp; &nbsp; &nbsp; <strong>Prohibition:</strong> This writ is issued by a higher court (High Court or Supreme Court) when a lower court has considered a case going beyond its jurisdiction.</p>
<p><strong>&nbsp; &nbsp; &nbsp; Quo Warranto:&nbsp;</strong>If the court finds that a person is holding office but is not entitled to hold that office, it issues the writ of quo warranto and restricts that person from acting as an office holder.</p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; Certiorari:&nbsp;</strong>Under this writ, the court orders a lower court or another authority to transfer a matter pending before it to the higher authority or court.</p>
<p><strong>Writ Jurisdiction of Supreme Court and High Courts</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</strong>Under article 32 of the Constitution, the Supreme Court can issue writs for the enforcement of the Fundamental Rights only. On the other hand, under Article 226 of the Constitution, the High Courts have been empowered to issue writs for the enforcement of the Fundamental Rights as well as other legal rights of the individuals. Thus, the writ jurisdiction of the High Courts is wider than that of the Supreme Court.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; Under Article 32 of the Constitution, the Supreme Court is bound to issue writs if an individual approaches the Apex Court on the violation of the Fundamental Rights. On the other hand, under Article 226 of the Constitution, the High Courts are not bound to issue writs. They can suggest some alternatives also. Thus, the Supreme Court has been made the custodian or the guardian of the Fundamental Rights of the citizens.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Originally there were seven fundamental rights but at present there number stand at six. Right to property is no longer a fundamental right. It is a legal right. The right to property was deleted from the list of Fundamental Rights by the 44<sup>th</sup> Amendment Act, 1978. It is made a legal right under Article 300A in Para XII of the Constitution.</p>
<p><strong>Doctrine of Basic Structure of the Constitution</strong></p>
<p>&nbsp; &nbsp; &nbsp; &nbsp;The Supreme Court in <strong>Kesavananda Bharti made a judicial innovation.&nbsp;</strong>This gave rise to the &lsquo;doctrine of basic structure of the Constitution&rsquo;. It held that the power of the Parliament to amend the Constitution is limited to the extent of not violating the basic structure of the Constitution. However, the court did not elaborate on what constitutes the basic structure of the Constitution. Court considers every legislation on case to case basis. Some of the judgements have withheld that Supremacy of constitution, Rule of law, Secularis etc. are part of the basic structure.</p>
<p><strong>Judicial Review and Fundamental Rights</strong></p>
<p> &nbsp; &nbsp; &nbsp; Judicial review is the power of Supreme Court and High Courts to declare a law as unconstitutional or void, if it is inconsistent with any of the provisions of the Constitution to the extent of such inconsistency. This power is available both against the law made by the legislature and an act of the executive.</p>
<p><strong>Armed Forces and Fundamental Rights</strong></p>
<p> &nbsp; &nbsp; &nbsp; <strong>Article 33&nbsp;</strong>empowers the Parliament to restrict or abrogate the fundamental rights of the members of armed forces, paramilitary forces, police forces, intelligence agencies and analogous forces. The objective of this provision is to ensure the proper discharge of their duties and the maintenance of discipline among them.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp;The power to make laws under Article 33 is conferred only on Parliament and not on state legislatures. Any such law made by Parliament cannot be challenged in any court on the ground of contravention of any of the fundamental rights.</p></div>
  <div class="menu-content Dpsp"><h1>DIRECTIVE PRINCIPLES OF STATE POLICY (DPSP)</h1>
<hr>
<p>Part IV of the Constitution relates to the Directive Principles of State Policy. It set forth the ideals and objectives to be achieved by the State for setting up in India a Social welfare state, as distinguished from a mere police State.</p>
<p>inspiration for including the Directive Principles in the Constitution is drawn from the <strong>Constitution of Ireland</strong>. The Directive Principles are meant to be the fundamental principles which should necessarily be made the basis of all executive and legislative actions that may be taken in the governance of the country.</p>
<p><strong>The 42<sup>nd</sup> Amendment</strong>, which came into force in January 1977, attempted to raise the status of the Directive Principles by stating that no law implementing any of the Directive Principles could be declared unconstitutional on the grounds that it violated any of the Fundamental Rights.</p>
<p>DPSP has been classified under following headings:</p>
<p><strong>Socialist Principles</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Article 38:&nbsp;</strong>States to secure a social order for the promotion of welfare of the people.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;The States shall strive to promote the welfare of the people by securing and protecting as effectively as it may a social order in which justice, social, economic and political, shall inform all the institutions of the national life.</p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; Article 39:&nbsp;</strong>This article lays down certain principles of policy to be followed by the State. The State shall, in particular, direct its policy towards securing:</p>
<ul>
    <li>That the citizens, men and women equally, have the right to an adequate means of livelihood;</li>
    <li>That the ownership and control of the material resources of the community are so distributed as best to subserve &nbsp;the common good;</li>
    <li>That the operation of the economic system does not result in the concentration of wealth and means of production to the common detriment;</li>
    <li>That there is equal pay for equal work for both men and women;</li>
    <li>That the health and strength of workers, men and women, and the tender age of children are not abused and that citizens are not forced by economic necessity to enter avocations unsuited to their age or strength;</li>
    <li>Those children are given opportunities and facilities to develop in a healthy manner and in conditions of freedom and dignity and that childhood any youth are protected against exploitation and against moral and material abandonment.</li>
</ul>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;Article 39A</strong>: States shall strive for providing equal justice and free legal aid &ndash; The States shall secure that the operation of the legal system promotes justice, on a basis of equal opportunity.</p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;Article 41&nbsp;</strong>: Right to work, to education and to public assistance in certain cases.</p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;Article 42</strong>: Provision for just and humane conditions of work and maternity relief &ndash; The State shall make provision for securing just and humane conditions of work and for maternity relief.</p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;Article 43&nbsp;</strong>: Living wage, etc. for workers.</p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;Article 43A :</strong> Participation of workers in management of industries.</p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;Article 45 :&nbsp;</strong>Provision for early childhood care and education to children below the age of six years.</p>
<p><strong>&nbsp;Gandhian Principles</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; Article 40:&nbsp;</strong>Organisation of village panchayats &ndash; The Staes shall take steps to organise village panchayats and endow them with such powers and authority as may be necessary to enable them to function as units of self-government.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>Article 46 :</strong> Promotion of educational and economic interests of Schedule Castes, Schedule Tribes and other weaker sections.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<strong>Article 47&nbsp;</strong>: Duty of the State to raise the level of nutrition and the standard of living and to improve public health.</p>
<p><strong>Article 48&nbsp;</strong>: Organisation of agriculture and animal husbandry.</p>
<p><strong>Liberal Principles</strong></p>
<p><strong>Article 39A</strong>: Equal justice and free legal aids.</p>
<p><strong>Article 44</strong>: Uniform civil code for the citizens.</p>
<p><strong>Article 50</strong>: Separation of judiciary from executive- The State shall take steps to separate the judiciary from the executive in the public services of the State.</p>
<p><strong>International Principles</strong></p>
<p><strong>Article 51:&nbsp;</strong>Promotion of international peace and security.</p>
