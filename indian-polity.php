  <div class="menu-content Vice"><h1>Vice-President</h1><hr>
<p>According to Article 65 the Vice-President act as President and discharges his functions during casual vacancies in the office, or during the absence. It provides for:&nbsp;</p>
<ul>
    <li>In the event of the occurrence of any vacancy in the office of the President by reasons of his death, resignation or removal, or otherwise the Vice-President shall act as President until the date on which a new President is elected to fill such vacancy enters upon his office.</li>
    <li>When the President is unable to discharge his functions owing to absence, illness or any other cause, the Vice-President shall discharge his functions until the date on which the President resumes his duties.</li>
</ul>
<p><strong>Election of Vice-President</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;</strong>According to <strong>Article 66(1)</strong>, the Vice-President shall be elected by the members of an electoral college consisting of the members of both Houses of Parliament in accordance with the system of proportional representation by means of the single transferable vote and the voting at such election shall be by secret ballot.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; According to <strong>Article 66(2)</strong>, the Vice-President shall not be a member of either House of Parliament or of a House of the Legislature of any State, and if a member of either House of Parliament or of a House of the Legislature of any State be elected Vice-President, he shall be deemed to have vacated his seat in that House on the date on which he enters upon his office as Vice-President.</p>
<p><strong>Prime Minister</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</strong>The Prime Minister theoretically holds office during the pleaser of the President Actually the Prime Minister stays in office as long as he enjoys the confidence of the Parliament. The normal term is five years but it is automatically reduced if the house is dissolved earlier.</p>
<p><strong>Powers</strong></p>
<ul>
    <li>The Prime Minister enjoys extensive powers.</li>
    <li>He is the Leader of the majority party in the Lok Sabha.</li>
    <li>He can recommend the dissolution of Lok Sabha to the President before expiry of its normal term.</li>
    <li>All the members of the Council of Ministers are appointed by the President on the recommendation of the Prime Minister.</li>
    <li>He allocates portfolios among the various ministers and reshuffles them according to his wishes. He can ask a minister to resign and even get him dismissed by the President.</li>
    <li>He presides over the meetings of the Council of Ministers and exercises a strong influence on its decisions.</li>
    <li>The Prime Minister is the chief channel of communication between the President and the Council of Ministers and keeps the former informed about all the decisions of the council.</li>
</ul>
<p><strong>Council of Ministers</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</strong>A minister must be a member of either House of Parliament. If a person who is not a member of either House of Parliament is appointed a minister, he shall cease to be a minister after six months, unless in the meanwhile he manages to get elected to either of the two Houses Ministers may be chosen from members of either House or a Ministers may be chosen from members of either House or a Minister who is a member of one House, has right to speak in and to take part in the proceedings of the other House though he has no right to vote in the House of which he is not member.</p>
<p>There are three types of Council of Ministers:</p>
<ul>
    <li><strong>Cabinet Ministers</strong>: Cabinet Ministers are the senior most ministers and they always head the departments or the Ministers if they are given one. A Cabinet Minister can be without a portfolio also. The Cabinet Ministers can take part in the cabinet meetings as a matter of right. The cabinet enjoys a constitutional status under Article 352, which says that the President can proclaim National Emergency on the written advice of the Cabinet.</li>
    <li><strong>Ministers of State</strong>: These Ministers are generally not given independent portfolios but the Prime Minister can allocate independent ministry to these ministers. They can attend cabinet meetings but only on an invitation extended to them by the Prime Minister.</li>
    <li><strong>Deputy Ministers</strong>: The Deputy Ministers are the junior most ministers. They never head a department and they can never attend the cabinet meetings.</li>
    <li><strong>Size of the Council of Ministers</strong></li>
</ul>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; The 91<sup>st</sup> Constitution (Amendment) Act, 2003 fixed the size of the Council of Ministers. The strength of the Council of Ministers including the Prime Minister shall no exceed 15% of the total strength of the Lower House.</p>
<p><strong>Principles on which the Cabinet System of Government Functions</strong></p>
<p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</strong>The parliamentary form of government is based on the principle of collective responsibility. Article 75(3) states that the Council of Ministers is collectively responsible to the Lok Sabha. It means that all the Ministers are collectively answerable to the Lok Sabha for the policies and decisions of the government, even though a decision taken may pertain to a single ministry. The individual ministers may have differences among themselves on certain issues but once a decision is taken by the cabinet it becomes a joint decision of all the Ministers. If a minister does not agree with the decision of the cabinet he has no choice, but to resign. The Council of Ministers works as a team. Thus it swims or sinks together.</p>
<p><strong>Individual Responsibility of Ministers</strong></p>
<p><strong>&nbsp; &nbsp;&nbsp;</strong>Apart from collectively responsible to the Lok Sabha, all ministers are individually responsible to the President. Article 75(2) declares that the ministers hold office during the pleasure of the President.</p></div>
