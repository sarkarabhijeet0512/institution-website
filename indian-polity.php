  <div class="menu-content High"><h1>High Courts</h1><hr>
<p>Chapter V of Part VI of the Constitution provides for the provisions for the High Courts. Article 214 holds that there shall be a High Court for each State. The High Court stands at the head of a State&rsquo;s judicial administration. The Centre has constituted three new High Courts in the northeast &ndash; Meghalaya, Manipur and Tripura &ndash; taking the total number of High Courts in the country from 21 to 24. So now there are 24 High Courts in the country, three having jurisdiction over more than one State. Among the Union Territories Delhi alone has a High Court of its own. Other six Union Territories come under the jurisdiction of different State High Courts. According to Article 216, each High Court comprises of a Chief Justice and such other judges as the President may, from time to time appoint.</p>
<p><strong>Appointment of the Judges</strong></p>
<p>The Chief Justice of a High Court is appointed by the President in consultation with the Chief Justice of India and the Governor of the State. The procedure for appointing puisne judges is the same except that the Chief Justice of the High Court concerned is also consulted.</p>
<p>According to Article 217(1) every Judge of a High Court shall be appointed by the President by warrant under his hand and seal after consultation with the Chief Justice of India, the Governor of the State, and, in the case of appointment of a Judge other than the chief Justice, the Chief Justice of the High Court and shall hold office, in the case of an additional or acting Judge, as provided in Article 224, and in any other case, until he attains the age of sixty-two years.</p>
<p><strong>Qualification of the Judges</strong></p>
<p>According to Article 217(2), a person shall not be qualified for appointment as a Judge of a High Court unless he is a citizen of India and</p>
<ul>
    <li>has for at least ten years held a judicial office in the territory of India; or&nbsp;</li>
    <li>has for at least ten years been an advocate of a High Court or of two or more such Courts in succession.</li>
</ul>
<p><strong>Important Provisions Regarding the Judges</strong></p>
<p><strong>Article 220&nbsp;</strong>puts restriction on practice after being a permanent Judge. This article reads as, &ldquo;No person who, after the commencement of this Constitution, has held office as a permanent Judge of a High Court shall plead or act in any court or before any authority in India except the Supreme Court and the other High Courts.&rsquo;&rsquo;</p>
<p><strong>Article 222&nbsp;</strong>provides for the provisions regarding the transfer of a Judge from one High Court to another. According to Article 222(1), the President may, after consultation with the Chief Justice of India, transfer a Judge from one High Court to any other High Court.</p>
