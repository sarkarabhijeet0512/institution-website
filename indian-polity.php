  <div class="menu-content Legislature"><h1>STATE LEGISLATURE</h1>
<p>The Constitution provides for a Legislature for every State. However, the question as to whether the State Legislature shall be unicameral (having one House only) or bicameral (having two Houses), is left for each State to decide of itself. The Constitution does not provide for bicameral legislature for all states. It provides bicameral legislature for U.P., Bihar, Maharashtra, Karnataka, Andhra Pradesh and Telangana. Jammu and Kashmir has bicameral legislature but under its own Constitution.</p>
<p><strong>Legislative Council</strong></p>
<p>The Legislative Council, commonly known as Vidhan Parishad, is the Upper House of the State Legislature. It is constituted as a permanent House. <strong>Article 171(1)&nbsp;</strong>provides that the total number of members in the Legislative Council of a State shall not exceed one-third of the total number of members in the Legislative Assembly of that State, but not less than 40 members in any case.</p>
<p><strong>Abolition or Creation of Legislative Councils</strong></p>
<p><strong>Article 169&nbsp;</strong>deals with the creation or abolition of Legislative Council in a State. Article 169 holds that if the State Legislative Assembly passes a resolution by a majority of not less than 2/3<sup>rd</sup> of the members present &amp; voting and by the majority of total strength of the House, requesting the Parliament to create or abolish the State Legislative Council then the Parliament may be law provide for the abolition and creation of the Legislative Council.</p>
<p>The initiative must come from the State Legislative Assembly. It is not binding on the Parliament. The Parliament passes the law by a simple majority.</p>
<p><strong>Composition of the House</strong></p>
<p>The Council shall be constituted as provided in clause (3) of Article 171. Article 171(3) states that of the total number of members of the Legislative Council of a State:</p>
<ul>
    <li>As nearly, as one- third shall be elected by electorates consisting of members of municipalities, district boards and such other local authorities in the State as Parliament may be law specify;</li>
    <li>As nearly as, one-twelfth shall be elected by electorates consisting of persons residing in the State who have been for at least three years graduates of any university in the territory of India or have been for at least three years in possession of qualifications prescribed by or under any law made by Parliament as equivalent to that of a graduate of any such university;</li>
    <li>As nearly as one-twelfth shall be elected by electorates consisting of persons who have been for at least three years engaged in teaching in such educational institutions within the State, not lower in standard than that of a secondary school, as may be prescribed by or under any law made by Parliament;</li>
    <li>As nearly as may be one-third shall be elected by the members of the Legislative Assembly of the State from amongst persons who are not members of the Assembly;&nbsp;</li>
    <li>The remainder shall be nominated by the Governor in accordance with the provisions of clause (5). The members to be nominated by the Governor shall consist of person having special knowledge or practical experience in respect of such matters as the following, namely: literature, science, art, cooperative movement and social service.</li>
</ul>
<p>The member of a legislative Council are elected in accordance with the system of proportional representation by means of the single transferable vote.</p>
<p><strong>Legislative Assembly</strong></p>
<p>The Constitution says that the Governor may appoint one member from the Anglo-Indian community if it is not adequately represented in the House. Rests of members are elected directly by the people from the territorial constituencies. <strong>Article170(1)</strong> holds that the Legislative Assembly of each State shall consist of not more than five hundred, and not less than sixty, members chosen by direct election from territorial constituencies in the State.</p>
<p>According to <strong>Article 172(1)</strong>, every Legislative Assembly of every State, unless sooner dissolve, shall continue for five years from the date appointed for its first meeting and no longer and the expiration of the said period of five years shall operate as dissolution of the Assembly.</p>
<p>Provided that the said period may, while a Proclamation of Emergency is in operation, be extended by Parliament by law for the period not exceeding one year at a time and not extending in any case beyond a period of six months after the Proclamation has ceased to operate.</p>
<p><strong>Passage of Bills</strong></p>
<p>Three Types of Bills are passed in State Legislature:</p>
<ul>
    <li>Ordinary bill&nbsp;</li>
    <li>Money bill</li>
    <li>Financial bill</li>
</ul>
<p>The passage of money bill is similar to the procedure followed at the Centre. An ordinary bill can originate in either House. If it originates in the Upper House and is duly passed and transmitted to the Lower House and if the Lower House rejects it, then the bill comes to an end. If a financial bill or an ordinary bill, originates in the Lower House and after its passage it is transmitted to the Upper House, the Upper House has 3 options. It can pass it, it can reject it or it may not take any action for 3 MONTHS.</p>
<p>After the lapse of 3 months, the bill goes to the Lower House, if the Lower House passes the bill for the second time, it is again transmitted to the Upper House and on the expiry of one month after the bill has been received for the second time, the bill is deemed to have been passed by the State Legislature. After that it goes to the Governor for his assent.</p>
<p><strong>Assent to Bills</strong></p>
<p><strong>Article 200</strong> deals with the procedures regarding the assent to the bills passed by the State legislature. It holds that, when a Bill has been passed by the &nbsp;Legislative Assembly of a State or, in the case of a State having a Legislative Council, has been passed by both Houses of the Legislature of the State, it shall be presented to the Governor and the Governor shall declare either that he assents to the Bill or that he withhold assent therefrom or that he reserves the Bill for the consideration of the President:</p>
<p>Provided that the Governor may, as soon as possible after the presentation to him of the Bill for assent, return the Bill if it is not a Money Bill together with a message requesting that the House or Houses will reconsider the Bill or any specified provisions thereof and, in particular, will consider the desirability of introducing any such amendments as he may recommend in his message and, when a Bill is so returned, the House or Houses shall reconsider the Bill accordingly and if the Bill is passed again by the House or Houses with or without amendment and presented to the Governor for assent, the Governor shall not withhold assent therefrom.</p>
<p>Provided further that the Governor shall not assent to, but shall reserve for the consideration of the President, any Bill which in the opinion of the Governor would, if it became law, so derogate from the powers of the High Court as to endanger the position which that Court is by this Constitution designed to fill.</p>
<p>According to Article 201, when a Bill is reserved by a Governor for the consideration of the President, the President shall declare either that he assents to the Bill or that he withholds assent therefrom:</p>
<p>Provided that, where the Bill is not a Money Bill, the President may direct the Governor to return the Bill to the House or, as the case may be, the Houses of the Legislature of the State together with such a message as is mentioned in the first proviso to Article 200 and, when a Bill is so returned, the House or Houses shall reconsider it accordingly within a period of six months from the date of receipt of such message and, if it is again passed by the House or Houses with or without amendment, it shall be presented again to the President for his consideration.</p></div>
