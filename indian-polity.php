  <div class="menu-content Preamble"><h1>THE PREAMBLE</h1><hr>
<p>Preamble is an integral part of the constitution. In it mentioned are the great ideals which our constitution desires to follow. The concept of India being a democratic state has its roots in the preamble. It provides the basic ideology upon which the entire constitution rests. Secularism, Sovereignty and Socialism as the state goals are enshrined in here. The guarantee of Equality, Freedom and Liberty are woven in here.</p>
<p>     It reads, &lsquo;WE, THE PEOPLE OF INDIA , having solemnly resolved to constitute India into a SOVEREIGN SOCIALIST SECULAR DEMOCRATIC REPUBLIC and to secure to all its citizens: JUSTICE, social, economic and political; LIBERTY of thought, expression , belief, faith and worship; EQUALITY of status and of opportunity; and to promote among them all, FRATERNITY assuring the dignity of the individual and the unity and integrity of the Nation: IN OUR CONSTITUENT ASSEMBLY this twenty-sixth day of November, 1949, do HEREBY ADOPT, ENACT AND GIVE TO OURSELVES THIS CONSTITUTION&rsquo;.</p>
<p ><strong>Preamble is a part of the Constitution</strong></p>
<p><strong>    </strong>Dr. D.D. Basu holds the view that &ldquo;a statute must be read as a whole and a Preamble is as much a part of the statute as its enacting part.&rdquo;</p>
<p>    The proceedings in the Constituent Assembly make it clear that the Preamble to our Constitution was adopted and enacted in the same manner as the rest of the Constitution. The Preamble was put to vote in the Constituent Assembly by a motion which stated that the &ldquo;<strong>Preamble stands part of the</strong> <strong>Constitution</strong>&rdquo; and the motion was adopted. It appears that this fact was not brought to the notice of the Supreme Court in <strong>Berubari Union case</strong>, wherein the Supreme Court had held that the Preamble was not a part of the Constitution and therefore it could never be regarded as a source of any substantive powers conferred on government under the provisions of the Constitution. Later, when the history of the drafting and the ultimate adoption of Preamble were brought to the notice of the Supreme Court in the <strong>Kesavananda Bharti case, the court laid down that the Preamble to the Indian Constitution was an integral part of the Constitution.</strong></p>
<p><strong>    </strong>Thus, after the observation of the Supreme Court in this case, there remains no doubt that the Preamble to the Indian Constitution is a part of the Constitution.</p>
<p><strong>Amendment of the Preamble</strong></p>
<p><strong>   </strong>It was contended in Berubarui Union Case that the Preamble though a part of the Constitution, but was not a provision of the Constitution and, therefore, it could not be amended by the Parliament under Article 368. Rejecting the contention the court in Kesavananda Bharti case held that being a part of the Constitution, the Preamble was not outside the reach of the amending power of the Parliament under Article 368. It was in the exercise of this amending power that the Constitution (42<sup>nd</sup> Amendment) Act, 1976 amended the preamble inserting therein the terms &lsquo;<strong>Socialist</strong>&rsquo;, &lsquo;<strong>Secular</strong>&rsquo;, and &lsquo;<strong>integrity</strong>&rsquo;.</p></div>
