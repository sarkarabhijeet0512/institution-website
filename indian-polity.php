  <div class="menu-content Duties"><h1>FUNDAMENTAL DUTIES</h1><hr>
<p>Rights and Duties go hand in hand. They are like two sides of a same coin. In India 40 years after independence it was felt that there was a definite need to make the people more conscious of their<strong>duties to</strong>wards the nation. In February 1976 <strong>the Swaran Singh Committee</strong> was established for suggesting constitutional reforms. In its report published in May 1976, this committee recommended, the incorporation of fundamental duties of the people of India.</p>
<p>Accepting this recommendation of the committee, the parliament passed the 42<sup>nd</sup> Amendment Act and by it introduced a new part, Part IVA, in the constitution. Article 51A was added to describe the fundamental duties of the citizen.</p>
<p>The following are the Fundamental Duties of every citizen of India:</p>
<ul>
    <li>To abide by the Constitution and respect the National Flag and the National Anthem;</li>
    <li>To cherish and follow the noble ideals which inspired our national struggle for freedom;</li>
    <li>To uphold and protect the sovereignty, unity and integrity &nbsp;of India;</li>
    <li>To defend the country and render national service when called upon to do so;</li>
    <li>To promote harmony and the spirit of common brotherhood amongst all people of India transcending religious , linguistic and regional or sectional diversities and to renounce practices derogatory to the dignity of women;</li>
    <li>To value and preserve the rich heritage of our composite culture;</li>
    <li>To protect and improve the natural environment including forests, lakes, rivers and wild life and to have compassion for living creatures;</li>
    <li>To develop the scientific temper, humanism and the spirit of inquiry and reform;</li>
    <li>To safeguard public property and to abjure violence;</li>
    <li>To strive towards excellence in all spheres of individual and collective activity, so that the nation constantly rises to higher levels of endeavor and achievement; and&nbsp;</li>
    <li>To provide opportunities for education by the parent, the guardian, to his child, or a ward between the ages of 6-14 years as the case may be.</li>
</ul></div>
