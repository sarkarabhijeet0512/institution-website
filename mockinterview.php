Preparation for the Interview is a continuous process. This involves a wide reading of books, journals, magazines and at least two newspapers. One should try to improve his/her conversational skills with the right pronunciation. The candidate should be prepared to answer questions on his background, hobbies and extracurricular activities. It is a good idea to discuss current affairs and recent issues with friends. One good way of rehearsing possible questions would be to have mock interviews and discussion groups. The candidate should make a self- analysis of his strengths and weaknesses and make a conscious effort to play on his strengths.<br><br>

​

A good mock Interview like od EMPRESS IAS is an indispensable requirement. One must take a number of mock interviews. You must understand that the main purpose of these mock interviews is to get over the anxiety of facing an interview board and not to revise UPSC syllabus and brush up your knowledge. Your having reached UPSC interview is a testimony of your knowledge and intelligence. Now, it’s all about testing your personality. It is very natural for anyone to feel nervous and jittery knowing that the toil and hard work of months and years is going to culminate into 30 mins of interview. Undertaking mock interviews is extremely important specially for those candidates who are appearing for IAS Interview for the first time.<br><br>

​

